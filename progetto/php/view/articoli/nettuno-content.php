	<h1><strong> NETTUNO </strong></h1>

	<h2><em>
		E' l'ultimo pianeta del sistema solare. Invisibile a occhio nudo, assomiglia a Urano anche nei bellissimi colori che illuminano la sua
		atmosfera nuvolosa, dovuti alla presenza del metano.
	</em></h2>

</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Nettuno" src="images/nettuno.jpg" width="600" height="450"></div>

<p>
	E' l'ottavo pianeta del sistema solare per lontananza dal Sole e l'ultimo pianeta gioviano. Per la geometria delle orbite di Nettuno e di
	Plutone, inoltre, capita che Nettuno si trovi più lontano dal Sole di quanto non lo sia plutone: è accaduto, ad esempio, negli anni
	1979-1999. Tuttavia, di solito è Plutone il più lontano: esso raggiunge una distanza massima dal Sole che è circa una volta e mezzo la
	massima lontananza raggiunta da Nettuno.</br>
	La presenza di un ottavo pianeta era già stata sospettata da <em>Adams</em> e <em>Le  Verrier</em> che evidenziarono alcune irregolarità nel
	moto orbitale di Urano, giustificabili con l'azione gravitazionale di un corpo esterno di grandi dimensioni. Ma Nettuno era al limite delle
	possibilità di osservazione degli strumenti dell'epoca, e la sua scoperta, realizzata dagli astronomi dell'osservatorio di Berlino il 23
	dicembre 1846 dopo una serie di pazienti indagini, fu il maggior risultato ottenuto nel corso dell'Ottocento. Se all'epoca apparve come un
	piccolo disco azzurro-verdognolo, sappiamo che, in realtà, non è certo piccolo: per dimensioni, segue solo Giove, Saturno e Urano.</br>
	Questo rimase pressoché tutto ciò che si sapeva su Nettuno, finchè nel 1989 fu raggiunto dalla sonda <em>Voyager2</em>, che ha cambiato il
	modo di pensare a questo pianeta. Nelle fotografie, infatti, Nettuno appare come un bellissimo pianeta blu, con sfumature, chiazze e tenui
	striature bianche. Sulla sua superficie si osserva una macchia scura grande quanto la Terra: come nel caso della grande macchia rossa di
	Giove, si trattava di una tempesta in condizioni stazionarie che, successivamente, è scomparsa, come mostrano alcune recenti immagini dello
	<em>Hubble</em>.</br>
	Poiché il <em>Voyager2</em> è passato a grande velocità e per breve tempo accanto al pianeta, ulteriori informazioni su Nettuno sono state
	raccolte sfruttando anche i più potenti telescopi a terra e lo <em>Hubble</em>.</br>
	Se il nome Nettuno ricorda quello del dio del mare e il suo aspetto blu-azzurro ricorda il colore dell'acqua, ciò non vuol dire che questo
	pianeta sia ricoperto di oceani: come nel caso di Urano, il colore dell'atmosfera (formata soprattutto da idrogeno ed elio) è dovuto a una
	piccola percentuale di metano. Come negli altri pianeti esterni, l'atmosfera impedisce di vedere la superficie gelata del pianeta: sconvolta
	da vortici e tempeste, è attraversata da venti che soffiano paralleli all'equatore raggiungendo i 2.000km/h. I venti di Nettuno sono i più
	veloci del sistema solare. La temperatura media è sui -200°C, e come gli altri pianeti esterni anche Nettuno emette più energia di quanta ne
	riceva. Come Urano ha un campo magnetico più debole di quello terrestre, un sistema di 4 anelli, stretti, molto scuri e molto tenui, fatti
	soprattutto di particelle di ghiaccio d'acqua con dimensioni che vanno dal milionesimo di millimetro ai pochi metri. A differenza degli altri
	noti, gli anelli di Nettuno non hanno una densità uniforme: in alcuni tratti è alta, mentre altrove la materia è molto rarefatta.</br>
	Al momento conosciamo 13 satelliti di Nettuno, tutti di dimensioni molto inferiori a quelle della nostra Luna. I più grandi sono: Proteo, il
	corpo più scuro del sistema solare che riflette solo il 6% della luce solare; Nereide, che segue una traiettoria molto ellittica e molto
	inclinata rispetto al piano equatoriale di Nettuno; Tritone, il più massiccio, che ruota in senso retrogrado.</br>
	In particolare, Tritone ha dimensioni simili a quelle della Luna, e suscita l'interesse dei planetologi per alcuni geyser di azoto gassoso
	che sono stati fotografati e che raggiungono molti chilometri di altezza, inoltre, ha un'atmosfera propria, un probabile contenuto del 25%
	d'acqua e una struttura rocciosa. La superficie, a temperatura bassissima (circa 34,5K) è presumibilmente ricoperta di metano, anidride
	carbonica e azoto congelati.
</p>


	<h1><strong> I PIANETI NANI O PLUTOIDI </strong></h1>

	<h2><em>
		La recente decisione della comunità astronomica internazionale ha definito un nuovo gruppo di corpi celesti: I PIANETI NANI.</br>
		Ne fanno parte corpi noti, come Cerere e Caronte, e corpi appena scoperti. Ci troviamo anche Plutone, che ormai ha perso il suo primato
		di pianeta più esterno del sistema solare.
	</em></h2>

</div> <!-- fine titoloContent -->

<div id="titoloContent">

	<h2><strong> CERERE </strong></h2>
	
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Cerere" src="images/cerere.jpg"></div>

<p>
	Scoperto nel 1801 da <em>Giuseppe Piazzi</em>, all'Osservatorio di Palermo, ha un diametro di circa 1.000km e finora è stato il più grande
	asteroide fra Marte e Giove
</p>

<div id="titoloContent">

	<h2><strong> CARONTE E PLUTONE </strong></h2>
	
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Caronte e Plutone" src="images/caronte-plutone.jpeg" width="550" height="483"></div>

<p>
	Formano un sistema doppio: Caronte dista da Plutone solo 17.000km, ed entrambi ruotano intorno a un centro gravitazionale mostrandosi sempre
	lo stesso emisfero. La loro origine è ancora incerta. Di Plutone si sa solo che è un corpo ghiacciato e roccioso che percorre l'orbita in 248
	anni, con il perielio (punto minimo di distanza dal Sole) di poco interno all'orbita di Nettuno.
</p>

<div id="titoloContent">

	<h2><strong> ERIS </strong></h2>
	
</div> <!-- fine titoloContent -->

<p>
	Gira intorno al Sole lungo un'orbita fortemente ellittica di 45°, e impiega circa 560 anni per compiere una rivoluzione completa. Sarebbe
	simile a Plutone, ma con un diametro di circa una volta e mezzo; sulla superficie avrebbe metano congelato. <strong>Dysnomia</strong> è il
	suo satellite, con un diametro di circa 250km.
</p>

<div id="titoloContent">

	<h2><strong> HAUMEA </strong></h2>
	
</div> <!-- fine titoloContent -->

<p>
	Grande quanto Plutone, compie una rotazione in 4h e perciò ha una forma allungata. Si trova nel sistema solare esterno e ha 2 satelliti: 
	<strong>Hi'iaka</strong> (o <strong>Rudolph</strong>) e <strong>Namaka</strong> (o <strong>Blitzen</strong>).
</p>

<div id="titoloContent">

	<h2><strong> MAKEMAKE </strong></h2>
	
</div> <!-- fine titoloContent -->

<p>
	Con un raggio inferiore ai 2.000km, ha un'orbita del tutto esterna a quella di Nettuno.
</p>


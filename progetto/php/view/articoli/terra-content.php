	<h1><strong> LA TERRA </strong></h1>
	<h2><em>
		La Terra: il terzo pianeta del sistema solare a partire dal Sole. Piuttosto vicina alla sorgente di energia più
		ricca della zona, ma non tanto da bruciarsi. Il nostro pianeta, come gli altri, ha una storia lunga circa 5 miliardi
		e mezzo di anni, ma solo negli ultimi secoli – da quando l'uomo ha incominciato a porsi domande e a darsi risposte
		non sempre verificate o verificabili – se ne sono sentite di cotte e di crude sul suo conto. E' piccola, è grande, è
		sferica, è piatta, è cilindrica, galleggia su un oceano d'acqua, è al centro dell'universo...<br />
		In tempi recenti, si è cercato di sgombrare il campo delle credenze sulla struttura e sulla dinamica della Terra.
		Numerosi scienziati di varie discipline, procedendo con attenzione sulla base di prove certe, di indagini
		tecnologicamente avanzate, di osservazioni attendibili, di ipotesi attentamente vagliate e verificate, hanno fatto
		progredire la conoscenza della Terra a passi da gigante. Ciò nonostante, sappiamo ancora molto poco di alcuni
		aspetti del nostro pianeta. </em></h2>
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="La Terra" src="images/terra.jpg" width="675" height="375"></div>

<p>
	La Terra è un corpo solido che, visto dallo spazio, sembra perfettamente sferico. In realtà, misurandola con i più accurati
	strumenti ci si accorge che non lo è. Esistono alcune zone dove il profilo si discosta in modo lieve, ma significativo, da
	quello di una sfera ideale alla quale si fa sempre riferimento. Il diametro che passa per i Poli, ad esempio, è un po' più
	corto di quello che passa per l'Equatore, e il raggio dell'Emisfero Nord è minore di quello dell'Emisfero Sud. Ci sono poi
	zone in cui la superficie è più schiacciata rispetto ad altre: la forma che più si avvicina a quella della Terra è la forma
	della pera.<br />
	La Terra è ricchissima di acqua: essa ricopre più del 71% della superficie del nostro pianeta, colorandolo d'azzurro e la si
	può trovare nello stesso momento, allo stato liquido, solido e gassoso.<br />
	Proprio il mondo dell'acqua costituisce una delle quattro “sfere” in cui viene suddiviso il pianeta Terra per poterlo
	studiare meglio: come l'<strong>atmosfera</strong>, la <strong>biosfera</strong>, la <strong>litosfera</strong> e
	l'<strong>idrosfera</strong>.<br />
	La <strong>litosfera</strong> è la parte più estesa e sconosciuta della Terra: masse rocciose allo stato solido e fluido si
	estendono per una profondità di oltre 6.000km fino al centro del pianeta mantenendo attiva una potente dinamica
	endogena.<br />
	L'<strong>idrosfera</strong>: una pellicola d'acqua – che raggiunge, al massimo, 9 decimillesimi del diametro terrestre –
	avvolge la litosfera. L'acqua è ovunque: scorre dentro le rocce, erode i fianchi dei monti, costruisce pianure e spiagge...
	L'acqua, la	più caratteristica sostanza della Terra, permette la vita, ne condiziona la distribuzione, governa il clima.
	<br />
	La <strong>biosfera</strong>: una sfera che non raggiunge i 20km di spessore: questo è lo spazio destinato alla vita. Fra
	cielo e mare, fra terra e aria, milioni di specie diverse nuotano, volano, corrono, scavano, saltano, strisciano... Un mondo
	brulicante di esseri enormi e microscopici sopravvive, mutando continuamente, in equilibrio precario.<br />
	L'<strong>atmosfera</strong>: aurore polari e fulmini, nubi dense e grigie e merletti di ghiaccio, venti che soffiano a
	300km l'ora e zone di calma perenne...E' l'atmosfera, un oceano di gas in continuo movimento che scherma la Terra dai raggi
	cosmici.
</p>

<div id="titoloContent">

	<h2><strong> La Terra nello Spazio </strong></h2>

	<h2><em> La Terra, come tutti i corpi nello spazio, si muove. Ma il suo movimento è molto complesso: per poterlo studiare
			meglio lo scomponiamo in vari moti più semplici che, ricomposti secondo le leggi della fisica, permettono di
			descriverlo in modo esatto e di chiarirne tutte le dinamiche. </em></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	I moti più importanti in cui viene suddiviso il movimento della Terra sono la <strong>rotazione</strong> intorno a un
	proprio asse, la <strong>precessione</strong>, e la <strong>rivoluzione</strong> intorno al Sole. Ciascuno di essi,
	combinandosi con le caratteristiche generali del nostro pianeta (fra le quali, fondamentale, l'inclinazione dell'asse di
	rotazione), produce numerosi effetti sul clima e sulle condizioni di vita della Terra, oltre che sulle possibilità di
	osservazioni astronomiche. Vediamo le caratteristiche essenziali di ognuno, e le loro conseguenze principali.<br />
	<strong>Rotazione</strong> e <strong>Processione</strong>: La Terra ruota intorno a un asse di rotazione che identifica
	sulla superficie terrestre il Polo Nord e il Polo Sud e che punta in direzione della Stella Polare, l'<em>α Polaris</em>
	della costellazione dell'Orsa Minore. Questa direzione è costante per periodi di tempo piuttosto lunghi ma, a causa del moto
	di processione, cambia nel corso delle migliaia di anni: come l'asse di una trottola, anche quello della Terra descrive un
	cono completo, impiegandoci 26.000 anni. L'asse di rotazione è inclinato rispetto all'<em>eclittica</em>, cioè al piano su
	cui si svolge la rivoluzione, di 66° 33' e mantiene questa direzione durante tutta la rivoluzione. Poiché la Terra è solida,
	la rotazione ha velocità angolare uguale per tutti i punti della sua superficie, eccetto che nei luoghi che si trovano
	sull'asse (Poli): in un giorno, ogni punto percorre una circonferenza completa (360°) la cui lunghezza dipende dalla
	latitudine.<br />
	La <strong>Rivoluzione</strong>: E' il moto che la Terra compie intorno al Sole considerato immobile al centro di uno dei
	due fuochi dell'eclittica. Essa rispetta le leggi di Keplero e di Newton: la velocità è maggiore al perielio e minore
	nell'afelio, anche se queste differenze non sono rilevanti dato che l'<em>eccentricità dell'orbita</em> è solo 0,017. In
	media, la Terra viaggia intorno al Sole a circa 30km/s.<br />

	<div align="center"><img id="stagioni" alt="Il cambio delle stagioni" src="images/terra-stagioni.png" width="450" height="208"></div>

	L'<strong>alternarsi delle stagioni</strong> è dovuto sia alla rivoluzione che all'inclinazione dell'asse. A seconda della
	posizione della Terra rispetto al Sole, cambia l'altezza sull'orizzonte della traiettoria seguita dal Sole e, di
	conseguenza, cambia la quantità di energia in arrivo. Da questo fenomeno dipendono direttamente le condizioni climatiche.
</p>

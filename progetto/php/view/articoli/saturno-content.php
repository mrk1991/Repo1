	<h1><strong> SATURNO </strong></h1>

	<h2><em>
		A partire dal Sole, Saturno occupa il sesto posto: poco meno grande di Giove, distante quasi un miliardo e mezzo di chilometri dalla
		nostra stella, è sicuramente il pianeta più famoso grazie ai suoi anelli luminosi, conosciuti e studiati fin dai tempi di Galileo.
	</em></h2>

</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Saturno" src="images/saturno.png" width="650" height="365"></div>

<p>
	Sebbene appaia più piccolo di Giove, sia per le dimensioni oggettive, sia per la maggior lontananza, Saturno si osserva bene anche con un
	buon binocolo. Di color bianco perlaceo, ha un bel sistema di anelli che, però, non è sempre visibile.</br>
	Saturno assomiglia molto a Giove: ha una spessa atmosfera di idrogeno (75% in massa) ed elio (25%) e si pensa che abbia un nucleo roccioso
	ricoperto di un oceano di idrogeno metallico liquido, di una coltre di idrogeno molecolare e ghiacci di vario genere. Come Giove ha una
	rotazione differenziale: la sua superficie visibile, infatti, non è solida, un fatto confermato anche dalla sua densità (inferiore a quella
	dell'acqua), la più bassa del sistema solare.</br>
	Saturno ha anche un'alta velocità di rotazione che, pur essendo inferiore a quella di Giove (10h40min invece che 9h50min) influenza sia la
	forma che la stratificazione atmosferica: lo schiacciamento ai poli e la colorazione in bande chiare e scure parallele all'equatore ne sono
	la conseguenza, come su Giove. Anzi, su Saturno la forma è ancora più schiacciata, mentre le bande parallele hanno profili meno netti e più
	larghi in prossimità dell'equatore.</br>
	Anche Saturno mostra formazioni atmosferiche a lungo termine, simili alla grande macchia rossa di Giove, e le diverse tonalità di colore
	delle bande sono dovute a differenze nella composizione chimica (composti di zolfo e di fosforo) e nello spessore delle nubi. Come Giove,
	inoltre, anche Saturno ha un intenso campo magnetico, e irradia più energia di quanta ne riceva dal Sole: al suo interno si raggiungono i
	12.000K, probabilmente per lo stesso processo di contrazione che si osserva su Giove.</br>
	Saturno però, affascina soprattutto per la meraviglia dei suoi anelli. Osservato per la prima volta da <em>Galileo</em> (che tuttavia non
	riuscì a capire che cosa fosse) l'anello di Saturno è formato, in realtà, da una numerosa successione di anelli concentrici, con diametri
	leggermente diversi. Essi sono costituiti da polveri, corpuscoli di ghiaccio e ghiaccio secco (anidride carbonica), roccia ghiacciata che
	orbitano intorno al pianeta.</br>
	Questo sistema di anelli ha una struttura molto complessa: al telescopio se ne vedono due o tre separati da spazi solo apparentemente vuoti.
	In realtà esso si estende senza soluzione di continuità per circa 65.000km: l'anello più interno orbita al limite dell'atmosfera, quello più
	esterno si allontana dal bordo visibile del pianeta di circa 250.000km. Il suo spessore, invece, è ridottissimo: in media qualche chilometro.
	Poiché tutti gli anelli sono disposti sul piano equatoriale del pianeta, e poiché l'asse di rotazione è inclinato rispetto al piano
	dell'orbita, osservati da terra essi cambiano aspetto col passare del tempo e, a intervalli di quasi 14 anni e mezzo, “scompaiono” alla
	vista, trovandosi di taglio rispetto all'osservatore.</br>
	L'origine degli anelli di Saturno (come degli anelli degli altri pianeti esterni) è ancora sconosciuta: potrebbero derivare da uno o più
	satelliti frantumati dalle forze di marea, ma è più probabile che siano ciò che resta del materiale primordiale che, a causa dell'eccessiva
	vicinanza dei grandi pianeti, non si è aggregato a formare un satellite. D'altra parte nessun pianeta ha catturato tanta “spazzatura
	spaziale” quanto Saturno: <em>Voyager 1</em> ha localizzato asteroidi, rocce cosmiche, ammassi di polveri, piccole lune e blocchi ghiacciati
	in quantità davvero enorme.
</p>


	<h2><strong> I satelliti di Saturno </strong></h2>
	
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Saturno e i suoi satelliti" src="images/saturno-satelliti.jpg" width="700" height="217"></div>

<p>
	Saturno, coi suoi 49 satelliti, 18 dei quali maggiori, ha intorno a sé un sistema solare in miniatura. Solo Titano però, conosciuto già prima
	dell'invio delle sonde, è abbastanza grande: ha le stesse dimensioni di Ganimede, è maggiore di Mercurio ed è interessante perchè ha
	un'atmosfera composta da azoto, metano, ammoniaca, idrocarburi e acetilene. Poichè si pensa che questa miscela di gas sia simile
	all'atmosfera della Terra primitiva, si ritiene che Titano sia uno dei corpi extraterrestri più adatti alla formazione della vita. La sonda
	<em>Huygens</em> (ESA) (che prende nome dall'astronomo Olandese che scoprì questo satellite) è atterrata sulla sua superficie inviando a
	terra un'enorme quantità di dati fisico-chimici dell'atmosfera e del suolo roccioso, di immagini e di rumori ambientali. Si pensa che queste
	condizioni, unite alla presenza d'acqua e all'assenza di un campo magnetico che protegga Titano quando, a volte, si trova fuori della
	magnetosfera di Saturno restando esposto al vento solare, possano aver contribuito a produrre un'evoluzione chimica verso la vita.
</p>

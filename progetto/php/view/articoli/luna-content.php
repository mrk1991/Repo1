	<h1><strong> LA LUNA </strong></h1>

	<h2><em> La Luna, in realtà piccolissima, ci appare grande come il Sole: per questo è stata adorata come una divinità di
			uguale importanza. Per la regolare variabilità del suo aspetto, è stata strumento di misura del tempo e simbolo di
			elementi contrastanti e contrapposti: da una parte Luna piena, luminosa e benevola, dall'altra Luna nuova, oscura e
			misteriosa. E ancora oggi, nonostante le conoscenze acquisite, nonostante l'uomo l'abbia raggiunta con sonde e
			spedizioni di ricerca, nonostante si siano chiariti molti problemi, e si sappia che la Luna è solo una palla di
			roccia che ruota nello spazio, sono ancora molti quelli che credono nella sua influenza sugli eventi terrestri, dal
			ritmo delle piogge alla nascita dei bambini. </em></h2>

</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="La Luna" src="images/luna.jpg" width="500" height="356"></div>

<p>
	La Luna, l'unico satellite naturale della Terra, ha una superficie vecchia, bucherellata e consumata: oltre ai crateri
	vulcanici, relativamente rari, è disseminata di vasti pianori lavici chiamati <em>mari</em> o <em>bacini</em>, che appaiono
	più scuri delle zone circostanti dette <em>terre</em> o <em>altopiani</em>. Bordati di catene di montagne con cañon, gole,
	depressioni e picchi, sono anch'essi costellati di crateri dovuti, prevalentemente, al bombardamento meteoritico avvenuto
	durante le ere più antiche.<br />
	La massa lunare, troppo piccola, non riesce a trattenere le molecole di gas: l'assenza di atmosfera e di liquidi che
	scorrano sulla superficie, ha fatto sì che le formazioni rocciose lunari si conservassero intatte così come si sono formate:
	le più antiche, che risalgono a circa 4 miliardi di anni fa, si ergono ancora accanto, o – spesso – sotto, quelle più
	recenti.<br />
	Nel complesso, dunque, la Luna è un piccolo mondo di roccia, con sbalzi di temperatura di oltre 200°C (media al Sole circa
	100°C, all'ombra circa -100°C). Il suolo, coperto da una polvere sabbiosa, impalpabile e finissima, vibra in media ogni anno
	per 3.000 debolissime scosse sismiche endogene. In compenso, la caduta di un meteorite di qualche decina di chili può
	provocare terremoti che fanno tremare la Luna per diversi minuti.
	
<div id="titoloContent">

	<h2><strong> Osservazioni </strong></h2>
	
</div> <!-- fine titoloContent -->
<p>
	La Luna brilla perché riflette la luce del Sole: un fenomeno, questo, già intuito dai popoli più antichi che misuravano il
	tempo in base alle osservazioni astronomiche, ma rimasto quasi sconosciuto fino ad Anassimene, che finalmente interpretò
	correttamente la dinamica delle eclissi. Ma fu Galileo con il suo cannocchiale a toccare con mano ciò che gli altri avevano
	solo intuito: la Luna è un oggetto fisico, aspro, roccioso, in tutto paragonabile alla Terra. Ma Galileo fa di più: come sua
	abitudine, misura <em>"dimostrando che le asperità terrestri sono di gran lunga minori delle lunari; minori, dico, anche
	parlando assolutamente, e non in ragione soltanto delle grandezze dei rispettivi globi"</em>. La topografia lunare nasce
	così. Ma per avere ulteriori informazioni sulla reale struttura della Luna e sulla sua topografia si dovranno attendere vari
	secoli: la parola definitiva sarà detta con le spedizioni spaziali russe, statunitensi e cinesi che fotograferanno anche la
	faccia nascosta. E' probabile, tuttavia, che sotto la morbida superficie del nostro satellite ci attendano nuove
	scoperte.
</p>

<div id="titoloContent">

	<h2><strong> Misure della Luna </strong></h2>
	
</div> <!-- fine titoloContent -->
<p>
	E' stato stabilito che la distanza Terra- Luna abbia un valore di 384.400 km, il preciso controllo di queste misure permette
	sia di verificare la teoria del moto lunare quantificando gli scambi energetici fra Terra e Luna, sia di misurare il
	rallentamento della rotazione terrestre, sia di raccogliere informazioni utili ad avvalorare i modelli sulla struttura
	interna della Luna.<br />
	La forma della Luna è molto simile a quella di una sfera: dalle misure di diametro terrestre, distanza media Terra-Luna e
	diametro angolare medio della Luna si calcolano facilmente il diametro, la superficie e il volume del nostro satellite. A
	causa dell'attrazione terrestre e della sua rotazione, anche la Luna ha una gibbosità in direzione della Terra: la
	differenza fra i due assi, però, è insignificante: circa 1km e mezzo, appena lo 0,43%.<br />
	La massa viene determinata in base a misure gravitazionali valutando le maree terrestri o le perturbazioni nell'orbita
	dell'asteroide Eros che si avvicina molto al sistema Terra-Luna.
</p>

<div id="titoloContent">

	<h2><strong> Geologia lunare </strong></h2>
	
</div> <!-- fine titoloContent -->

<img class="sinistra" alt="Crateri lunari" src="images/crateri.jpg" width="300" height="174">

<p>
	Sebbene l'esplorazione diretta della Luna abbia dato una risposta definitiva a molti interrogativi, anche l'osservazione da
	terra fornisce preziose informazioni sulla geologia lunare: dal numero dei crateri, e dalla loro stratificazione si può
	ricavare l'età relativa delle rocce di una regione (più sono numerosi e sovrapposti, più l'area è antica); misurando la
	diffusione della luce solare  si può dire che il suolo lunare deve essere formato prevalentemente da particelle piccole e
	incoerenti con un'elevata porosità.; tecniche radar possono sondare la crosta lunare fino a una profondità di 1.300m , e
	così via. Molte altre informazioni si ottengono con tecniche di telerilevamento, con osservazioni al telescopio e con lo
	studio delle fotografie. Infine, le perturbazioni gravitazionali misurate dalle sonde in orbita lunare permettono di
	valutare la distribuzione, nella Luna, di masse con densità differenti: è così che sono stati scoperti i <em>mascon</em>,
	anomalie gravitazionali negative in corrispondenza di bacini circolari dovute, forse, allo sprofondamento in tempi remoti di
	enormi asteroidi.<br />
	<strong>Mari o bacini:</strong> chiamate mari perché a causa del loro colore più scuro si pensava fossero distese d'acqua,
	le pianure lunari, o bacini, sono in realtà enormi distese di roccia basaltica formatasi probabilmente in epoca remota
	quando l'impatto di qualche grande meteorite ha spezzato la crosta solida facendo fuoriuscire dall'interno materiale ancora
	fluido. I bacini occupano il 15% dell'intera superficie lunare e sono quasi del tutto assenti sulla faccia nascosta da
	terra.<br />
	<strong>Continenti o altopiani:</strong> chiamati così in contrapposizione ai mari, sono ciò che resta della crosta
	primitiva: qui le tracce del millenario bombardamento meteoritico sono più numerose.<br />Rappresentano l'85% circa della
	superficie lunare (il 70% della faccia visibile da terra) e le loro rocce sono molto meno dense di quelle dei bacini (circa
	3 volte l'acqua) e molto più antiche (circa 4 miliardi di anni).<br />
	La crosta della Luna non è come quella terrestre, divisa in placche in movimento: le <strong>catene montuose lunari</strong>
	non sono prodotte dalla dinamica interna ma dal bombardamento meteoritico. La minor gravità ha contribuito poi a formare
	monti che, in proporzione, sono molto più alti di quelli terrestri come notava anche Galileo: raggiungono spesso i 6km e i
	picchi di 8km sono diffusi.<br />
	D'altra parte che la Luna non abbia moti interni è evidente: le strutture geologiche lunari non si spostano di un millimetro
	e il flusso di calore interno è troppo basso perché possa esistere una dinamica tettonica sostenuta da correnti convettive.
	Le misure fatte durante la spedizione <em>Apollo17</em> indicano un flusso di calore endogeno di soli 2-4 mW/cm₂; circa la
	metà di quello terrestre. A volte, più che di catene montuose si tratta di precipizi che separano due altopiani a quote
	diverse: anche in questo caso, si riesce a collegarne l'origine a qualche impatto.<br />
	Anche se, sulla Luna, in mancanza di atmosfera, d'acqua e di vita, non esistono rocce simili a quelle sedimentarie
	terrestri, anche se le rocce lunari sono quasi completamente degassate e anidre (prive d'acqua), sono essenzialmente simili
	a quella della Terra, perfino la loro composizione isotopica corrisponde.<br />
	Ma la maggior parte della superficie lunare non è rocciosa, ma ricoperta dalla <strong>regolite</strong>, un materiale
	costituito da frammenti con una granulometria molto variabile. Lo strato di regolite ha uno spessore minimo nei bacini, dove
	varia da pochi cm a 5m, e uno spessore massimo sugli altopiani, dove arriva a 30m.
</p>


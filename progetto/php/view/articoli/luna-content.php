	<h2><strong> Moti e fenomeni lunari </strong></h2>
	
</div> <!-- fine titoloContent -->

<img class="destra" alt="Fasi lunari" src="images/fasi.jpg" width="300" height="225">

<p>
	<strong>Rotazione:</strong> la Luna ruota intorno al proprio asse polare: rispetto alla perpendicolare al piano della
	rivoluzione, l'asse è inclinato di 6°41'. Ciò implica che:<br />
	- il giorno lunare ha solo minime variazioni stagionali;<br />
	- il giorno lunare ha minime differenze di durata alle </br>diverse latitudini;<br />
	- il Sole arriva sempre all'orizzonte dei Poli lunari;<br />
	Inoltre, poiché il periodo e il verso di rotazione sono uguali al periodo e al verso di rivoluzione, la Luna espone alla
	Terra sempre la stessa metà: è un fenomeno dovuto all'attrazione terrestre che, ha frenato la rotazione lunare
	originaria.<br />
	<strong>Rivoluzione:</strong> l'orbita che la Luna segue intorno alla Terra è, approssimativamente, un'ellisse di cui la
	Terra occupa uno dei fuochi. Il moto si svolge da Ovest a Est, nello stesso senso della rotazione e della rivoluzione
	terrestri. Il periodo di rivoluzione lunare, cioè il tempo che intercorre fra due passaggi della Luna nello stesso punto
	dell'orbita, è detto anche mese siderale: ha una durata di 27,32 giorni solari medi.<br />
	<strong>Le fasi lunari:</strong> l'illuminazione della faccia lunare rivolta a noi cambia a seconda della posizione del
	nostro satellite rispetto alla Terra e al Sole. I diversi aspetti con cui ci appare la Luna sono detti fasi, e i principali
	prendono nomi diversi:<br />
	- Fase di <strong>Luna nuova (o novilunio):</strong> si ha quando Sole, Luna e Terra, in quest'ordine, sono sulla stessa
	linea; la Luna è invisibile perché sorge e tramonta insieme al Sole: di giorno non la vediamo perché ci mostra il lato non
	illuminato, di notte semplicemente non c'è.<br />
	- Fase di <strong>Luna crescente:</strong> si verifica dopo la Luna nuova; la parte illuminata diventa da spicchio
	sottilissimo, sempre più estesa. A volte, in questo periodo, si può vedere la parte buia della Luna brillare di una debole
	luminosità: è la luce cinerea. La Luna, infatti, riflette verso di noi la luce che le giunge dalla Terra illuminata dal
	Sole.<br />
	- Fase di <strong>primo quarto:</strong> la congiungente Terra-Luna è a 90° dalla congiungente Terra-Sole; si vede ¼ della
	superficie lunare (prima metà del disco).<br />
	- Fase di <strong>Luna piena o plenilunio:</strong> Sole, Terra e Luna, in quest'ordine, sono sulla stessa linea; tutto il
	disco lunare appare illuminato perché la Luna sorge quando il Sole tramonta.<br />
	- Fase di <strong>Luna calante:</strong> dopo la Luna piena, la parte illuminata della Luna diventa sempre meno estesa, fino
	a ridursi ad uno spicchio.<br />
	- Fase di <strong>ultimo quarto:</strong> la congiungente Luna-Terra e la congiungente Terra-Sole sono di nuovo a 90°, ma la
	Luna è dalla parte opposta al primo quarto; si vede  ¼ della sua superficie (seconda metà del disco, quella che resta oscura
	al primo quarto). Le fasi di primo e di ultimo quarto sono dette anche quadrature.<br />
	Il tempo che passa tra due momenti successivi in cui la Luna assume lo stesso aspetto viene detto mese sinodico o lunazione
	e dura 29,53 giorni solari medi.
</p>

<div id="titoloContent">

	<h2><strong> Le eclissi </strong></h2>
	
</div> <!-- fine titoloContent -->
	
<img class="sinistra" alt="Eclisse lunare" src="images/eclissi.jpg" width="300" height="187">
	
<p>
	- Se la fase di Luna piena si verifica quando la Luna si trova in un punto della sua orbita vicina all'eclittica, il cono
	d'ombra della Terra la può oscurare: si ha un' <strong>eclisse parziale di Luna.</strong><br />
	- Se la fase di Luna piena si verifica quando la Luna si trova in un nodo, i centri di Sole, Terra e Luna, in quest'ordine,
	si trovano allineati: la Luna entra completamente nel cono d'ombra del nostro pianeta e si ha un'<strong>eclisse totale di
	Luna.</strong> La Luna in eclisse non scompare mai completamente ma assume una colorazione rossastra: l'ombra prodotta dalla
	Terra, infatti, è sempre debolmente illuminata dai raggi rossi del Sole che vengono rifratti dall'atmosfera terrestre come
	in un immenso tramonto.<br />
	- Se la fase di Luna nuova si verifica mentre la Luna è in un punto della sua orbita vicino all'eclittica, il cono d'ombra
	che essa produce può oscurare una parte della superficie terrestre: si ha un'<strong>eclisse parziale di Sole.</strong>
	Visto da terra, il disco d'ombra della Luna copre solo parzialmente quello del Sole.<br />
	- Se la fase di Luna nuova si verifica quando la Luna si trova in un nodo, i centri di Sole, Luna e Terra, in quest'ordine,
	sono allineati: tutto il cono d'ombra della Luna si proietta sulla Terra e si ha un'<strong>eclisse totale di Sole.</strong>
	Visto da terra, il disco oscuro della Luna – che ha un diametro apparente quasi uguale a quello solare – copre
	progressivamente tutto il disco del Sole oscurandolo per pochi secondi. In quegli attimi di buio totale si possono vedere le
	stelle e le parti del Sole che di solito sono invisibili (ad esempio la corona). La Luna però, procede nel suo moto, e
	l'ombra spazza ampie aree del nostro pianeta.<br />
	Quando la Luna si trova sull'apogeo, il suo disco ha un diametro apparente un po' più piccolo di quello del Sole: se in
	queste condizioni si verifica un'eclisse totale di Sole, la Luna non copre tutto il disco luminoso e si ha un'<strong>eclisse
	anulare.</strong><br />
	In media, ogni 18 anni avvengono circa 70 eclissi: 42 di Luna e 28 di Sole.
</p>

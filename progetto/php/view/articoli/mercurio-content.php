<p>
	Mercurio è un pianeta davvero speciale e, per molti aspetti, ancora tutto da scoprire: a causa della notevole vicinanza al Sole, infatti, è
	molto difficile osservarlo da terra. In media dista “solo” 43 milioni di chilometri dalla superficie visibile della nostra stella,
	allontanandosi fino a un massimo di 28° dal bordo solare. Perciò da terra Mercurio è visibile solamente all'alba o al tramonto. Ha anche un
	moto di rivoluzione molto rapido (altrimenti sarebbe già precipitato sul Sole): compie un percorso completo in 88 giorni, quindi lo si può
	osservare solo per pochi giorni di seguito, e poi scompare. Da terra, inoltre, come nel caso della Luna e di Venere, anche nel caso di
	Mercurio si osservano le <strong>fasi</strong>: farlo con un binocolo o un telescopio, però, può essere pericoloso per gli occhi, e bisogna
	far molta attenzione a non puntare lo strumento direttamente sul Sole. Oltre alle poche informazioni carpitegli con un'enorme pazienza dagli
	astronomi del passato (Mercurio è noto fin dall'antichità) abbiamo una consistente mole di dati grazie alle esplorazioni realizzate dalle
	sonde spaziali. In particolare, nel 1974 il pianeta venne esplorato da <em>Mariner 10</em>, una sonda statunitense che si avvicinò fino ad
	alcune centinaia di chilometri dalla superficie trasmettendo circa 6.000 fotografie, coprendo il 40% circa della superficie. Così, per la
	prima volta, si sono potuti osservare “da vicino” i riarsi panorami di Mercurio, sottoposti alla fortissima radiazione solare.</br>
	Mercurio gira su se stesso (giorno siderale) in 58,65 giorni terrestri, ma perchè il Sole torni sullo stesso punto della superficie ci
	vogliono 176 giorni terrestri: in questo periodo il pianeta gira 2 volte intorno alla nostra stella. Questa lenta rotazione espone al Sole lo
	stesso emisfero per lunghi periodi: arroventato a più di 430°C, Mercurio si raffredda nella lunga notte fino a -135°C (se è l'afelio, cioè
	quando si ha la massima distanza dal Sole, anche -180°C). Questa escursione termica è la più alta del sistema solare: in media 500°C fra la
	notte e il giorno. Ciò è dovuto anche all'assenza di atmosfera: flagellato dal vento solare, con una gravità decisamente bassa (è il più
	piccolo pianeta del sistema) Mercurio ha intorno a sé un velo gassoso fatto prevalentemente di elio.</br>
	Mercurio ha molto in comune con la Luna: per la sua posizione è stato bersagliato dalle meteoriti catturate dal Sole, ed è letteralmente
	ricoperto di crateri. Anche le zone pianeggianti sono molto simili ai “bacini” lunari, presumibilmente prodotte anch'esse da colate laviche.
	La formazione più rilevante su Mercurio è il <strong>Mare Caloris</strong>, un cratere ampio quasi 1.400km, profondo 9km, circondato da
	rilievi alti circa 2km: si pensa che abbia avuto origine meteoritica, circa 3,54 miliardi di anni fa. La spaventosa collisione ha rischiato
	di spezzare il pianeta: agli antipodi del Mare Caloris, infatti, si osserva una fitta rete di fratture dovute con tutta probabilità al
	contraccolpo dell'impatto.</br>
	Sulla struttura interna si possono solo fare ipotesi: in base alla presenza di un debole campo magnetico e all'elevatissima densità, si pensa
	che Mercurio abbia un nucleo di ferro del diametro di 3.600km circa (circa l'80% del diametro dell'intero pianeta).</br>
	Mercurio, infine, non ha satelliti.
</p>

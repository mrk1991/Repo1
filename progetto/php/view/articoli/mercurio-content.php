	<h1><strong> MERCURIO </strong></h1>

	<h2><em>
		E' il pianeta più vicino al Sole, il più denso dopo la Terra, il più piccolo, e anche quello in cui lo sbalzo termico fra notte e giorno
		è il più drammatico: in media 550°C! Da un mezzogiorno davvero di fuoco (427°C) a una mezzanotte da brivido (-123°C) in poco più di 500
		ore.
	</em></h2>


	<h1><strong> LE STELLE </strong></h1>

	<h2><em>
		Il Sole è la stella che conosciamo meglio: sappiamo molto dei processi nucleari che alimentano la sua produzione energetica e sappiamo
		molto anche sui fenomeni superficiali, sulla loro origine, sulla loro evoluzione. Tuttavia, anche del Sole non sappiamo poi molto, e la
		ricerca solare impegna ancora numerosissimi studiosi. Come si fa, allora, a dire qualcosa su altre stelle? Come si possono avere
		informazioni attendibili su quei puntini luminosi che anche il più forte telescopio continua a mostrarci puntiformi, tanto sono lontani?
		L'ingegno e la fantasia di alcuni scienziati hanno trovato come analizzare i sottili raggi di luce che arrivano fino a noi da anni-luce
		di distanza in modo da trarne mille informazioni.
	</em></h2>

</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Cielo stellato" src="images/stelle.jpeg" width="600" height="393"></div>

<p>
	Le stelle sono grandi corpi gassosi costituiti prevalentemente da idrogeno. Essi irradiano energia a ogni lunghezza d'onda a causa delle
	reazioni nucleari che si verificano al loro interno. Nate dalla contrazione gravitazionale di grandi nubi di materia interstellare, le
	stelle continuano a produrre energia finché le reazioni di fusione nucleare sviluppano più energia di quanta non ne occorra per avvenire.
	Irradia energia a spese della propria massa, e non avendo una massa infinita, prima o poi si spegne. Quando la massa della stella è pari ad
	almeno 20 masse solari, ciò che resta dopo l'esplosione è ancora sufficiente a innescare ulteriori processi. La forza di gravità prende il
	sopravvento assoluto, provocando l'inarrestabile concentrazione di tutta la massa. Poiché per ogni massa sferica, esiste un raggio limite
	che provoca un'estrema distorsione nello spazio-tempo intorno alla massa, separandola dal resto dell'universo, niente di ciò che viene
	emesso dalla stella ha più energia sufficiente per allontanarsi dalla sua superficie e la stella si trasforma in un buco nero diventando
	completamente invisibile.</br>
	Osservando il cielo, le stelle appaiono come migliaia di puntini luminosi, diversi per intensità, colore e dimensione, che si trovano
	stampati su di un'unica superficie a disegnare le più svariate forme. Sin dai tempi antichi infatti, nonostante esse occupino zone contigue
	del cielo solo per effetto prospettico, essendo distanti fra loro a volte per migliaia di anni luce, è stato possibile raggruppare le stelle
	più luminose in modo da formare quelle figure a cui si è dato il nome di <strong>costellazioni</strong>.</br>
	Le stelle si sono meritate inoltre nel corso dei secoli l'appellativo di <strong>fisse</strong>, anche se in effetti, al pari di tutti i
	corpi del sistema solare, esse si muovono (<strong>moto proprio</strong>). Il loro moto però è talmente lento che per notare degli
	spostamenti	bisognerebbe attendere millenni. Questo perché, a differenza dei pianeti, esse si trovano ad una distanza talmente grande da
	rendere l'angolo che deriva dallo spostamento quasi impercettibile.
</p>

<div align="center"><img class="principale" alt="Angolo spostamento stelle" src="images/stelle-fig1.jpg"></div>

<div id="titoloContent">
	<h2><strong> Magnitudine stellare </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	Le stelle si distinguono in base alla <strong>magnitudine relativa</strong> (luminosità apparente), una scala di valori centrata sullo zero,
	corrispondente al valore della stella <strong>Vega</strong>, con i valori più alti espressi con numeri negativi. La differenza fra le prime
	e le ultime è di circa 1 a 500, vale a dire che le stelle di magnitudine 1 saranno 500 volte più luminose di quelle dell'ultima classe
	(25).</br>
	Un'attenta valutazione va posta dunque alle distanze ed alle dimensioni stellari, che se non correttamente valutate possono portare a
	considerazioni errate. Il Sole infatti, una stella di medie dimensioni, che è anche la più vicina a noi (dista in media 149,6 milioni di
	chilometri, pari a 8 minuti luce), ci sembra ben più grande e luminoso di tante altre stelle, che pur emettendo luce per migliaia di volte
	tanto, appaiono molto deboli e minuscole a causa della loro lontananza.</br>
	Per ovviare a questo problema, e considerando che l'intensità della luce diminuisce col quadrato della distanza della sorgente, si usa
	allora la <strong>magnitudine assoluta</strong> (luminosità effettiva), ossia si considerano i corpi stellari come posti tutti alla stessa
	distanza, fissata per convenzione in 10parsec, equivalenti a circa 32 anni luce.
</p>

<div id="titoloContent">
	<h2><strong> Metodi per la misurazione delle distanze stellari </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	Per risalire alla distanza stellare un metodo molto usato è quello che sfrutta il fenomeno della <strong>parallasse annua</strong>. Infatti,
	considerando il nostro pianeta in un punto qualsiasi della sua orbita e puntando da esso una stella <strong>x</strong>, dopo sei mesi, quando
	la Terra sarà in un punto esattamente opposto, si vedrà lo stesso astro spostato sullo sfondo celeste di un angolo che sarà tanto più piccolo
	quanto esso sarà distante da noi. Misurando dunque l'entità di tale angolo, e conoscendo il raggio dell'orbita terrestre (pari a 1 Unità
	Astronomica), dalla trigonometria avremo la distanza D = 1 / tgA espressa in parsec.
</p>
	<div align="center"><img class="principale" alt="La distanza stellare" src="images/stelle-fig2.jpg"></div>
<p>
	Tuttavia per le stelle più lontane, essendo l'angolo risultante talmente piccolo da non poter essere misurato, si usano altri metodi come
	quello <strong>spettroscopico</strong> o quello delle <strong>cefeidi</strong>.</br>
	Il primo metodo consiste nello scomporre la luce della stella nelle sue componenti fondamentali facendola passare attraverso un	prisma.
	Analizzandola si notano le bande colorate dello spettro che risultano separate da righe oscure, che non sono altro che assorbimenti da parte
	dei gas che compongono il corpo stellare. Da queste è dunque facile risalire alla composizione chimica ed alla magnitudine assoluta	delle
	stelle, che poi posta a confronto con quella apparente ci darà la distanza.</br>
	Il secondo metodo utilizza le cefeidi, stelle che hanno la caratteristica di variare in modo regolare la loro luminosità secondo un periodo
	ben determinato, il quale è direttamente proporzionale alla stessa intensità luminosa. Dunque più lungo sarà questo periodo, maggiore
	risulterà la magnitudine assoluta, dalla quale otterremo poi quella apparente e quindi la distanza.</br>
	Il nome cefeidi deriva da <em>Delta Cephei</em> che fù la prima stella con queste proprietà ad essere stata scoperta.
</p>

<div id="titoloContent">
	<h2><strong> Le costellazioni </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	L'osservazione celeste non sarebbe cosa agevole senza l'adozione di un sistema che permetta di discernere agevolmente una zona dall'altra.
	Infatti, unendo le stelle più luminose con delle linee immaginarie, è possibile individuare determinati settori del cielo allo stesso modo
	di come è già stato fatto dagli antichi astronomi, i quali erano soliti interpretare questi allineamenti come raffigurazioni di miti,
	personaggi, eroi e leggende della loro vita quotidiana.</br>
	Con il termine <em>costellazioni</em> non s'intendono tuttavia solo delle figure, ma anche determinate aree, che esse rappresentano,
	e che comprendono stelle ed oggetti di altra natura quali: galassie, nebulose, ammassi stellari, ecc..
</p>

<div align="center"><img class="principale" alt="L'unione di alcune stelle con una linea immaginaria" src="images/stelle-fig3.jpg"></div>

<div id="titoloContent">
	<h2><strong> Origine delle costellazioni </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	Attualmente le costellazioni ufficiali sono 88 e più della metà di esse ci sono state tramandate dall'astronomo greco <em>Tolomeo</em> che,
	raccogliendo le testimonianze e gli studi precedenti, ne elencava 48 nel suo <strong>Almagesto</strong>. Il nome di molte di loro è quindi
	per la maggior parte di origine babilonese o greca, ed è stato mantenuto fino ai giorni nostri, così come quello di alcune stelle.</br>
	Tutto questo per quanto riguarda il cielo boreale, essendo quello dell'altro emisfero, o quanto meno la parte più a Sud di esso, nascosto,
	per via della latitudine, agli osservatori di tutta l'area mediterranea. Le costellazioni australi sono dunque di origine molto più recente,
	risalgono infatti agli ultimi 4-5 secoli, quando i circumnavigatori della Terra, esplorando quelle zone, si servirono del cielo meridionale
	per orientarsi durante la navigazione. Fornirono così preziose indicazioni agli astronomi di quel tempo che stilando nuove mappe celesti
	completarono la nostra conoscenza in merito.</br>
	Fra questi ricordiamo <em>Plancius</em>, <em>Bartsch</em>, <em>Hevelius</em>, <em>De Lacalle</em> e soprattutto <em>Bayer</em> (autore fra
	l'altro di un celebre atlante), che introdusse un sistema per indicare le stelle adoperando le lettere dell'alfabeto greco secondo un'ordine
	decrescente di luminosità. A compimento dell'opera di questi illustri studiosi, nomi e confini di ogni costellazione vennero definitivamente
	fissati, rispettivamente nel 1922 e nel 1930, dall'<strong>I.A.U.</strong>, il massimo organo astronomico mondiale.</br>
	Le costellazioni sono comunque delle entità di natura prospettica, formate da corpi che appaiono come facenti parte di un unico sistema, ma
	che in realtà nello spazio tridimensionale hanno in comune solo lo stesso settore celeste, essendo a volte situati a distanze di milioni di
	anni luce gli uni dagli altri.
</p>

<div align="center"><img class="principale" alt="Distanza Terra-Orione" src="images/stelle-fig4.jpg"></div>

<div id="titoloContent">
	<h2><strong> Visibilità delle costellazioni </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	Nonostante le stelle possano considerarsi fisse, ogni costellazione muta la sua posizione (rispetto alla nostra e non allo sfondo celeste)
	per effetto del moto apparente del cielo. Questo perchè la Terra, oltre a girare su se stessa, compie anche un moto di rivoluzione attorno
	al Sole, offrendoci ogni notte una visuale della volta celeste leggermente diversa. Ognuna di esse allora, con il passare dei mesi, apparirà
	alla stessa ora sempre più spostata verso Ovest, sino a quando scomparirà sotto l'orizzonte occidentale per riapparire poi, dopo un certo
	periodo di tempo, da quello orientale.</br>
	Fanno eccezione le costellazioni attigue ai poli che, per effetto della sfericità della Terra, ruotano attorno ad essi descrivendo dei
	cerchi completi fra lo <strong>zenit</strong> (punto immaginario situato sopra la testa dell'osseratore) e l'orizzonte. Per questo sono
	dette <strong>circumpolari</strong>, e sono visibili solo dall'emisfero di cui fa parte il polo celeste attorno a cui ruotano, rimanendone
	invece occultate nell'altro. Si distinguono in <strong>boreali</strong> ed <strong>australi</strong>.
</p>

<div id="titoloContent">
	<h2><strong> Costellazioni dello Zodiaco </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	Un discorso a parte meritano quelle 12 costellazioni che fanno parte dello <strong>zodiaco</strong>, ovvero di quella striscia del cielo
	concentrica all'eclittica, su cui si spostano apparentemente tutti i corpi principali del sistema solare. Fra questi uno in particolare, il
	Sole, attraversa ogni mese un settore pari a 30° (<strong>segno zodiacale</strong>) effettuando quindi un giro completo in un anno solare.
	</br>
	Per convenzione, si è allora stabilito sin dall'antichità, di identificare ognuna di queste suddivisioni con l'omonima e corrispondente
	costellazione. A partire dalla prima sono: <strong>Ariete</strong>, <strong>Toro</strong>, <strong>Gemelli</strong>,
	<strong>Cancro</strong>, <strong>Leone</strong>, <strong>Vergine</strong>, <strong>Bilancia</strong>, <strong>Scorpione</strong>,
	<strong>Sagittario</strong>, <strong>Capricorno</strong>, <strong>Acquario</strong> e <strong>Pesci</strong>. Tuttavia dello zodiaco fa
	parte anche la costellazione di <strong>Ofiuco</strong>, che il Sole attraversa in Dicembre, la quale però non è compresa fra i 12 segni
	zodiacali tradizionali.
</p>

<div id="titoloContent">
	<h2><strong> Precessione degli Equinozi </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	A causa del fenomeno della <strong>"precessione degli equinozi"</strong>, un lento e continuo movimento dell'asse di rotazione del nostro
	pianeta, che oscillando descrive un cerchio in 26000 anni, ogni riferimento celeste varia la sua posizione di circa 1,5° ogni secolo. Ogni
	2000 anni si verificherà allora uno slittamento pari a 30°, con il risultato che ogni costellazione occuperà il posto del segno zodiacale
	immediatamente successivo. A causa di ciò gli equinozi, le intersezioni dell'eclittica con l'equatore celeste, detti anche <strong>"punto
	d'Ariete"</strong> e <strong>"punto della Bilancia"</strong>, si trovano ai giorni nostri rispettivamente nei Pesci e nella Vergine e non
	più in quelle da cui anticamente presero il nome.
</p>

<div id="titoloContent">
	<h2><strong> GUIDA: Come individuare alcune costellazioni e stelle importanti del cielo visibile alle nostre latitudini </strong></h2>
</div> <!-- fine secondo titolo content> -->

<p>
	Per "muoversi con gli occhi" nel cielo è conveniente determinare delle relazioni geometriche che intercorrono tra le stelle o tra gruppi di
	esse. Ora si indicherà come tracciare degli allineamenti nel cielo a partire da alcune costellazioni di facile individuazione. Questi
	allineamenti anche se non dovessero essere perfetti, consentono di memorizzare con semplicità il contesto nel quale le stelle cercate sono
	poste, perché risultano più agevoli queste operazioni che non la ricerca delle singole posizioni delle stesse. Vediamo immediatamente come
	individuare le stelle più luminose e le costellazioni più importanti.
</p>

<div id="titoloContent">
	<h2><strong> La Stella Polare e il Nord </strong></h2>
</div> <!-- fine secondo titolo content> -->

<div align="center"><img class="principale" alt="Determinazione della Stella Polare a partire dal Grande Carro" src="images/stelle-fig5.jpg"></div>

<p>
	Con l'aiuto del <strong>Grande Carro</strong>, parte o <em>asterisma</em> della costellazione dell'<strong>Orsa Maggiore</strong>, a partire
	dalle due stelle situate dalla parte opposta al timone, tracciamo una linea immaginaria passante per le stesse,	riportando circa 5 volte la
	distanza che le separa: questa linea intercetterà la <strong>Stella Polare</strong>.</br>
	Queste due stelle del Grande Carro hanno i nomi arabi di <strong>Merak</strong> e <strong>Dubhe</strong> e proprio per questo motivo
	geometrico vengono denominate <em>puntatrici</em>.</br>
	Cadere in errore è difficile in quanto, in questa parte di cielo, la Stella Polare è quella di luminosità apparente maggiore, pur non
	essendo di per sé molto luminosa. Essa è molto importante, anzi sarebbe meglio dire fondamentale se si pensa al servigio che ha prodotto,
	per millenni, agli uomini in viaggio; la Polare, infatti, è l'unica stella che non appare muoversi durante la notte! Dista per l'appunto
	circa 1° dal polo del cielo: il punto attorno al quale tutto il cielo apparirà ruotare, in senso antiorario, nell'arco della notte. Tale
	punto si chiama <strong>Polo Nord Celeste</strong>, proiettando dal quale la perpendicolare all'orizzonte, si otterrà il punto cardinale
	<em>Nord</em>.
</p>

<div id="titoloContent">
	<h2><strong> Dal Grande Carro a Cassiopeia, Lira, Bovaro, Corona Boreale, Vergine, Leone e Gemelli </strong></h2>
</div> <!-- fine secondo titolo content> -->

<div align="center"><img class="principale" alt="Allineamenti a partire dal Gran Carro" src="images/stelle-fig6.jpg"></div>

<p>
	Il Grande Carro, alto in primavera, è un oggetto chiave nella geografia del cielo settentrionale, quello prossimo alla Stella Polare.
	Determinato il <strong>Piccolo Carro</strong>, dalla parte opposta al Grande Carro rispetto alla Polare, brilla la " W " di
	<strong>Cassiopeia</strong>.</br>
	Dall'immagine si desume il modo di procedere per localizzare la brillante stella <strong>Vega</strong> nella costellazione della <strong>
	Lira</strong>, particolarmente appariscente in estate. Proseguendo lungo una linea che conserva la curvatura del timone del Grande Carro si
	intercetta la stella <strong>Arturo</strong> nella costellazione del <strong>Bovaro</strong>, molto evidente in primavera, e quindi
	<strong>Spica</strong> la stella più brillante della costellazione della <strong>Vergine</strong>. Arturo è la stella più luminosa a Nord
	dell'equatore celeste, di magnitudine -0,1 e la quarta in assoluto del cielo. E' una gigante rossa, cento volte più luminosa del Sole ma con
	una superficie più fredda (da qui il colore rosso) distante 36 anni luce. Spica è di magnitudine 1, è bianco-azzurra e dista 260 anni luce
	(Spica è un termine latino che significa spiga di grano). Rispetto al Bovaro, dalla parte opposta del Grande Carro, si trova la
	costellazione della <strong>Corona Boreale</strong>, costituita da una serie di stelle disposte a semicerchio tra le quali brilla
	<strong>Gemma</strong>, di magnitudine 2,2. A partire dalle stelle <strong>Megrez</strong> e <strong>Phecda</strong> del Grande Carro si
	incontra la costellazione del <strong>Leone</strong>, intercettando la stella principale <strong>Regolo</strong>. Regolo è di
	magnitudine 1,4; è una stella bianco-azzurra che dista 77 anni luce. Con una linea congiungente Megrez e Merak si intercettano le stelle
	<strong>Castore</strong> e <strong>Polluce</strong> nella costellazione zodiacale dei <strong>Gemelli</strong>. Polluce è una gigante di
	colore arancione distante 33,7 anni luce dalla Terra, ed è la stella più luminosa dei Gemelli, con una magnitudine di 1,15.</br>
</p>

<div id="titoloContent">
	<h2><strong> Da Orione all'Auriga, Gemelli, Cane Minore, Cane Maggiore, Toro e Pleiadi </strong></h2>
</div> <!-- fine secondo titolo content> -->

<div align="center"><img class="principale" alt="Allineamenti a partire da Orione" src="images/stelle-fig7.jpg"></div>

<p>
	L'immagine sopra illustra <strong>Orione</strong> che costituisce la costellazione principale del cielo settentrionale in inverno. Il
	prolungamento della linea che congiunge le tre stelle della <strong>Cintura di Orione</strong> punta verso <strong>Sirio</strong>, la stella
	più luminosa del cielo, nella costellazione del <strong>Cane Maggiore</strong>. Sirio è un vertice di un triangolo molto ampio, conosciuto
	come <em>Triangolo Invernale</em>; gli altri due vertici sono rappresentati da <strong>Betelgeuse</strong> in Orione e <strong>Procione
	</strong> nel <strong>Cane Minore</strong>.	Betelgeuse (traducibile dall'arabo come mano) è una supergigante rossa variabile in luminosità,
	oscillante tra le magnitudini 0 e 1,3 in 6 anni circa; si trova a 430 anni luce di distanza. Procione è di magnitudine 0,4 ed il suo nome
	significa prima del cane, intendendo con questo che sorge prima dell'altra <em>"stella del cane"</em>, cioè Sirio. E' una stella bianca
	posta a 11,4 anni luce e, come Sirio, ha una nana bianca per compagna, <strong>Procione B</strong>, risolvibili solo con telescopi
	professionali.</br>
	Da <strong>Rigel</strong> che è la seconda stella in Orione, prolungando una semiretta passante per Betelgeuse, si incontrano
	Castore e Polluce, nella confinante costellazione dei Gemelli. Rigel è una supergigante blu, di	magnitudine 0,2 distante 770 anni luce.</br>
	Sull'altro fianco del <em>"Cacciatore"</em> Orione si trovano <strong>Aldebaran</strong> la stella più luminosa della costellazione del
	<strong>Toro</strong>; nella stessa direzione si trova anche l'ammasso aperto delle <strong>Pleiadi</strong>. Aldebaran è una gigante rossa
	che subisce variazioni irregolari di luminosità, tra le magnitudini di 0,75 e 0,95. Dista da noi 65 anni luce.</br>
	A Nord di Orione si trova <strong>Capella</strong>, la stella più brillante in <strong>Auriga</strong>. Capella ha una magnitudine di 0,08 e
	a occhio nudo appare di colore giallognolo; dista dalla Terra 42 anni luce.
</p>

<div id="titoloContent">
	<h2><strong> Il Triangolo Estivo: le costellazioni dell'Aquila, della Lira e del Cigno </strong></h2>
</div> <!-- fine secondo titolo content> -->

<div align="center"><img class="principale" alt="Il Triangolo Estivo" src="images/stelle-fig8.jpg"></div>

<p>
	L'immagine sopra mostra tre costellazioni ben visibili alle nostre latitudini durante i periodi estivi. Le stelle principali
	<strong>Vega</strong> nella <strong>Lira</strong>, <strong>Deneb</strong> (che in arabo significa coda) nella costellazione del
	<strong>Cigno</strong> e <strong>Altair</strong> nell'<strong>Aquila</strong>, formano un triangolo noto con il nome di <em>Triangolo
	Estivo</em>. Il Cigno, oltre ad essere inconfondibile a causa della sua caratteristica forma (conosciuto anche come la <em>"Croce del Nord"
	</em>), è completamente immerso nelle nubi di stelle della <strong>Via Lattea</strong> (parte di cielo in cui sono maggiormente addensate le
	stelle della nostra Galassia).</br>
	Vega è la quinta stella per luminosità del cielo, una stella bianca a 25 anni luce dal Sole, di magnitudine 0,0. Deneb è una brillante
	supergigante bianco-azzurra distante 3000 anni luce e di magnitudine 1,3: tra le stelle di prima grandezza è la più lontana. Altair è una
	delle venti stelle più luminose del cielo, di magnitudine 0,8, a soli 17 anni luce di distanza.
</p>


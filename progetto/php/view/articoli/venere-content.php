	<h1><strong> VENERE </strong></h1>

	<h2><em>
		Venere è un pianeta molto simile, per dimensioni, alla Terra. Come la Terra ha alcuni vulcani, spenti da poco, ma le somiglianze
		finiscono qui: pianeggiante per il 65% della superficie, Venere è avvolta da una spessa atmosfera di anidride carbonica, spazzata da
		venti violenti come uragani, riscaldata dall'effetto serra fino a una temperatura media di quasi 500°C.
	</em></h2>

</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Venere" src="images/venere.jpg" width="500" height="490"></div>

<p>
	Come Mercurio, Venere è un pianeta interno e orbita nello spazio compreso fra il Sole e la Terra. Per questo motivo, anche Venere si può
	osservare meglio al tramonto o all'alba. Se è visibile al tramonto è il primo astro a comparire nel cielo, e per questo gli antichi lo
	chiamavano <em>Vespero</em> (dal nome latino del tramonto), o <em>stella della sera</em>. Se è visibile all'alba, precede di poco il Sole e
	per questo gli antichi lo chiamavano <em>Lucifero</em> (che in latino vuol dire “portatore di luce”), o <em>stella del mattino</em>.
	Osservare Venere a occhio nudo, o con un binocolo o un telescopio, è davvero molto facile: può tramontare (o sorgere) fino a 3 ore dopo (o
	prima) del Sole. Per questo motivo, già nell'antichità se ne conoscevano bene i moti e le fasi, infatti, a causa del suo moto più vicino al
	Sole rispetto a quello della Terra, Venere viene illuminata in modo diverso (come accade alla Luna) e, vista da terra, passa da una fase
	crescente a una fase decrescente, fino a scomparire per poi riprendere il ciclo. A differenza della Luna, però, Venere, durante le fasi,
	cambia molto anche nelle dimensioni del suo diametro apparente: a causa della forte differenza di distanza dalla Terra, infatti, quando si
	trova in fase piena (2.60 * 10⁸km) ha un diametro apparente di 10 d'arco, mentre quando è in fase di falce (“solo” 0,4 * 10⁸km) ha un
	diametro apparente addirittura di 66 d'arco.</br>
	Le osservazioni di questo fenomeno permisero a <em>Galileo</em> di proporre una delle più concrete prove a convalida della teoria
	copernicana. A differenza dell'orbita di Mercurio, che è molto inclinata rispetto all'eclittica, l'orbita di Venere è inclinata di soli 3,4°.
	Per questo il pianeta è in alcuni periodi completamente oscurato dal Sole (<strong>occultazione</strong>) e in altri ne attraversa il disco
	da una parte all'altra (<strong>transito</strong>): in questo caso, appare come il dischetto nero proiettato sullo sfondo luminoso del Sole.
	Diversamente dalla maggior parte degli altri pianeti, inoltre, Venere ruota intorno al proprio asse di rotazione in senso retrogrado: chi
	vivesse sul pianeta vedrebbe il Sole sorgere a Ovest e tramontare ad Est.
</p>


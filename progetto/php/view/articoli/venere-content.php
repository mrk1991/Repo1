<div id="titoloContent">
	<h2><strong> Un pianeta come la Terra, ma molto diverso </strong></h2>
</div> <!-- fine titoloContent -->

<p>
	Venere è molto simile alla Terra: le dimensioni, la massa, la densità, la gravità, la presenza di una spessa atmosfera gassosa (responsabile,
	per l'alto potere riflettente, della sua lucentezza) sono le caratteristiche che accomunano questo pianeta con il nostro. Per questa ragione
	si è creduto per molto tempo che ospitasse forme di vita simili a quelle terrestri e, in cerca di queste forma di vita, sono state inviate
	numerose spedizioni spaziali dagli Stati Uniti e dall'Unione Sovietica. Tuttavia tutti i tipi di sonde che hanno esplorato l'atmosfera del
	pianeta e la sua superficie, hanno fatto crollare ogni speranza di trovare anche il più piccolo essere vivente: Venere assomiglia alla Terra,
	ma non poi tanto.</br>
	I dati raccolti sulla costituzione dell'atmosfera, sulle caratteristiche della superficie e sulla composizione del suolo, infatti, hanno
	permesso di conoscere abbastanza bene le condizioni “medie” di questo pianeta inospitale.
</p>

<div id="titoloContent">
	<h2><strong> Un'atmosfera ”pesante” e un suolo vulcanico </strong></h2>
</div> <!-- fine titoloContent -->

<p>
	La spessa coltre atmosferica venusiana, alta circa 85km, è formata per il 96% da anidride carbonica e alla base sviluppa una pressione circa
	92 volte più elevata di quella che si misura sulla Terra alla superficie del mare. L'acqua, nell'atmosfera di Venere così come su tutto il
	pianeta, è decisamente pochissima, e le nubi, sospese a una quota media di 50km, sono costituite prevalentemente da acido solforico. Per
	avere un'idea delle dimensioni dell'atmosfera venusiana, possiamo ricordare che, sulla Terra, quasi tutta l'aria si accumula nei primi 5,6km
	e che tutti i fenomeni meteorologici hanno luogo esclusivamente nei primi 10km sopra il suolo. Non è un caso che, dallo spazio, si riesca a
	vedere con facilità il suolo della Terra, mentre la superficie di Venere resta invisibile anche ai più potenti strumenti: la rifrazione è
	troppo forte. Ciò che si osserva al telescopio, dunque, non è la superficie di Venere, ma la sommità della sua atmosfera: l'anidride
	carbonica, infatti, è un gas che riflette le radiazioni visibili e infrarosse in modo molto efficiente. Per questa stessa ragione, la
	temperatura media del pianeta è estremamente elevata: vicina ai 500°C, addirittura più alta che su Mercurio. Questo valore (decisamente
	troppo alto per essere prodotto unicamente dall'irradiazione solare, anche se questa è comunque maggiore rispetto a ciò che arriva sulla
	Terra) viene spiegato come il risultato di millenni di pesantissimo effetto serra dovuto proprio all'alta percentuale di anidride carbonica
	atmosferica. Inoltre nell'alta atmosfera soffiano venti a una velocità di più di 400km/h: la velocità di rotazione del pianeta, infatti,
	varia molto se si considerano le nubi o il suolo. Mentre le nubi compiono un giro in poco più di 4 giorni, il suolo impiega circa 243 giorni
	a fare un giro completo. Poiché l'anno venusiano (cioè il tempo impiegato per compiere un'intera rivoluzione intorno al Sole) è di 224
	giorni, il giorno del suolo di Venere è più lungo dell'anno. Per questa ragione, al suolo il vento non supera mai i pochi chilometri all'ora.
	Ma non basta. Le apparecchiature sganciate da <em>Venera10</em> che sono riuscite ad atterrare indenni, sono andate distrutte dopo poche ore
	di funzionamento soprattutto a causa della forte pressione. Tuttavia in quelle ore hanno inviato dati inequivocabili: sotto l'enorme cappa di
	gas del pianeta la luce del Sole penetra a malapena e la luminosità del cielo venusiano è pari a quella terrestre in un giorno di nebbia
	fitta. In questa caldissima penombra, inoltre, ogni tanto cadono scrosci di acido solforico misto ad acqua. Le indagini delle sonde ci hanno
	permesso di sapere molto anche sul suolo di Venere: in larga parte pianeggiante, ha sia profonde depressioni, come la <strong>Venus Rift
	Valley</strong> che arriva a 4km di profondità ed è lunga 1.400km, sia rilievi che superano i 1.000m. Inoltre, si sospetta che Venere abbia
	avuto un'intensa fase di vulcanesimo, sostenuto da una tettonica a placche simile a quella della Terra, anche se attualmente non si registra
	alcuna emissione vulcanica.</br>
	Venere non ha satelliti, né un campo magnetico: è probabile che quest'ultima caratteristica sia dovuta al lentissimo moto di rotazione del
	pianeta intorno al proprio asse.
</p>

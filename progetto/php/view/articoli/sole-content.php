<div id="titoloContent">
	<h1><strong> IL SOLE: LA NOSTRA STELLA </strong></h1>
	<h2><em>
		Dopo che Galileo si è bruciato gli occhi sul Sole, la nostra stella ha smesso di essere perfetta: presenta cicli di macchie scure, ruota
		su se stessa e, a guardare bene, mostra “fiamme” altissime, “lampi” e macchie di luce... Ma perché l'attività solare possa essere
		studiata in modo quantitativo e le osservazioni spiegate con una teoria, si deve arrivare all'Ottocento, alle scoperte sull'atomo e alla
		relazione di Einstein che lega materia ed energia. Oggi sappiamo molto sulla dinamica del Sole, e anche se resta ancora molto da
		scoprire, ciò che sappiamo ci serve per capire tutte le altre stelle. Il Sole, infatti, non è speciale, anche se gli vogliamo bene: non è
		grande come una gigante, né piccolo come una nana, né instabile come una nova... Continuerà la sua placida esistenza ancora a lungo.
	</em></h2>
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Il Sole" src="images/sole.jpg" width="500" height="462"></div>

<p>
	Il Sole è una tra le numerose stelle a possedere un proprio sistema planetario, il sistema solare, costituito da tutti i corpi che sono
	mantenuti in orbita attorno alla stella dalla sua attrazione gravitazionale. Questi si suddividono in: pianeti, pianeti nani, corpi minori.
	I pianeti del sistema solare sono otto: Mercurio, Venere, Terra, Marte, Giove, Saturno, Urano e Nettuno. Il Sole è l'unica stella la cui
	forma possa essere apprezzata semplicemente alla vista. Utilizzando poi un modesto telescopio, dotato di un adeguato filtro o utilizzato in
	modo da proiettare l'immagine della stella su uno schermo bianco, è possibile osservare agevolmente le macchie solari e i brillamenti. La
	combinazione delle dimensioni e della distanza dalla Terra al Sole e dalla Terra alla Luna, è tale che i due astri si presentano nel cielo
	pressappoco col medesimo diametro apparente; tale situazione è all'origine di periodiche occultazioni della stella da parte del nostro unico
	satellite naturale, che prendono il nome di eclissi solari; le eclissi totali, in particolare, consentono di visualizzare la corona solare e
	le protuberanze. Il Sole è una stella di terza generazione la cui formazione sarebbe stata indotta dall'esplosione, circa 5 miliardi di anni
	fa, di una o più <strong>supernova</strong> nelle vicinanze di un'estesa nube molecolare del <em>Braccio di Orione</em>. E' accertato che,
	circa 4,57 miliardi	di anni fa, il rapido collasso della nube, innescato dalle supernove, portò alla formazione di una generazione di
	giovanissime stelle, tra le quali anche il Sole, che, subito dopo la sua formazione, assunse un'orbita quasi circolare attorno al centro
	della Via Lattea. Il Sole è attualmente in una lunga fase di stabilità durante la quale l'astro genera energia attraverso la fusione
	(all'interno del suo nucleo), dell'idrogeno in elio; la fusione nucleare inoltre fa sì che la stella sia in uno stato di equilibrio, sia
	idrostatico (ossia non si espande, né si contrae), sia termico. Una stella come il Sole impiega, considerando la massa, circa 10 miliardi di
	anni per esaurire completamente l'idrogeno nel suo nucleo. Al termine di questo periodo di stabilità, tra circa 5 miliardi di anni, il Sole
	entrerà in una fase di forte instabilità che prende il nome di gigante rossa. Questo collasso determinerà un incremento termico tale da
	innescare la fusione dell'idrogeno negli strati superiori, che provocheranno l'espansione della stella fino ad oltre l'orbita di Mercurio.
</p>

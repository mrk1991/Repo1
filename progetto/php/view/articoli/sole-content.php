	<h2><strong> Struttura </strong></h2>
	
</div> <!-- fine secondo titolo content> -->

<p id="sole-struttura">
	Il Sole possiede una struttura interna ben definita. La struttura interna del Sole, come quella delle altre stelle, appare costituita di
	involucri concentrici. Ogni strato possiede caratteristiche e condizioni fisiche ben precise, che lo differenziano dal successivo.</br>
	Gli strati sono, dal centro verso l'esterno:
</p>
	<ol class="paragrafo">
		<li>Il nucleo</li>
		<li>La zona radiativa</li>
		<li>La tachocline</li>
		<li>La zona convettiva</li>
		<li>La fotosfera, che è la superficie del Sole</li>
		<li>L'atmosfera, suddivisa in:</li>
			<ul>
				<li>Cromosfera</li>
				<li>Zona di transizione</li>
				<li>Corona</li>
			</ul>
	</ol>

	<div align="center"><img class="principale" alt="La struttura del Sole" src="images/sole-struttura.jpg" width="400" height="300"></div>
<p>
	<strong>Nucleo</strong>: È qui che avvengono le reazioni di fusione nucleare, fonte principe dell'energia solare. Gli astrofisici ritengono
	che il nucleo solare abbia una densità superiore a 150.000kg/m³ (150 volte quella dell'acqua), una temperatura di circa 13.600.000K ed una
	pressione di quasi 500 miliardi di atmosfere; la combinazione di simili valori favorisce la fusione nucleare dell'idrogeno in elio.</br>
	<strong>Zona radiativa</strong>: Situata all'esterno del nucleo, assorbe l'energia da esso prodotta e la trasmette per irraggiamento agli
	strati superiori. In questa fascia avviene il trasferimento dell'energia prodotta nel nucleo verso lo strato superiore, la zona convettiva.
	Una recente analisi dei dati raccolti dalla missione <em>SOHO</em> suggerisce che la velocità di rotazione della zona radiativa sia
	leggermente inferiore a quella del nucleo.</br>
	<strong>Zona di transizione</strong> (<strong><em>Tachocline</em></strong>): La zona di transizione tra la porzione radiativa e quella
	convettiva prende il nome di <em>tachocline</em>.</br>
	<strong>Zona convettiva</strong>: La zona convettiva ha uno spessore di circa 200.000km e si trova nella porzione esterna del Sole. L'area è
	caratterizzata da temperature e densità inferiori a quelle degli strati sottostanti; di conseguenza, energia e calore non possono essere
	trasferiti per irraggiamento, ma attraverso moti convettivi. La materia più calda e meno densa viene portata in superficie, dove cede parte
	della propria energia termica; una volta raffreddata, la materia risprofonda alla base della zona convettiva, dove riceve nuovamente il
	calore proveniente dalla zona radiativa. Nella zona convettiva la materia è in costante movimento.</br>
	<strong>Fotosfera</strong>: La fotosfera è lo strato superficiale del Sole, al di sotto del quale la stella diviene opaca alla luce
	visibile; si tratta dunque del primo strato visibile, dal quale l'energia proveniente dall'interno è libera di propagarsi nello spazio. È
	sede di fenomeni come le <strong>macchie solari</strong> e i <strong>flare</strong>. Poiché gli strati più alti della fotosfera sono più
	freddi di quelli più profondi, l'immagine del Sole appare più luminosa al centro, e si fa via via più tenue man mano che si procede verso il
	bordo del perimetro del disco visibile; questo fenomeno è chiamato <strong>oscuramento al bordo</strong>, ed è causato da un fenomeno di
	prospettiva.</br>
	<strong>Atmosfera</strong>: Gli strati al di sopra della fotosfera costituiscono l'atmosfera solare e sono, in ordine: la
	<strong>cromosfera</strong>, la <strong>zona di transizione</strong> e la <strong>corona</strong>. Questi sono molto più caldi della
	superficie solare. Qui si trova anche lo strato più freddo del Sole: si tratta di una fascia chiamata <strong>regione di minima
	temperatura</strong>, posta circa 500km sopra la fotosfera: quest'area, che ha una temperatura di 4000K, è sufficientemente fredda da
	consentire l'esistenza di alcune molecole, come il monossido di carbonio e l'acqua.
</p>


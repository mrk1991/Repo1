	<h2><strong> Macchie solari </strong></h2>
	
</div> <!-- fine secondo titolo content> -->

<div align="center"><img class="principale" alt="Le macchie solari" src="images/sole-macchie.jpg" width="500" height="307"></div>

<p>
	Osservando il Sole con filtri adatti, è possibile scorgere lungo la sua superficie le caratteristiche <strong>macchie fotosferiche</strong>,
	aree ben definite che appaiono più scure rispetto al resto della fotosfera a causa della loro temperatura più "bassa" (dell'ordine dei
	4500K). Le macchie solari più grandi possono estendersi anche per migliaia di chilometri. Il numero di macchie solari visibili sulla
	superficie del Sole non è costante, varia durante il ciclo solare.
</p>


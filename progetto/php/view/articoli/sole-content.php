<p>
	Quando anche l'idrogeno dello strato superiore al nucleo sarà totalmente convertito in elio (entro poche decine di milioni di anni) si avrà
	un nuovo collasso e la stella subirà una riduzione delle proprie dimensioni; i prodotti di fusione, non impiegabili in nuovi cicli
	termonucleari a causa della piccola massa della stella, si accumuleranno inerti nel nucleo; così, avverrà un successivo collasso che
	determinerà la produzione di una quantità di energia talmente elevata da provocare una nuova espansione dell'astro, che raggiungerà
	dimensioni circa 100 volte superiori a quelle attuali, tanto che la sua atmosfera arriverà ad inglobare molto probabilmente Venere. Incerto è
	il destino della Terra: alcuni astronomi ritengono che anche il nostro pianeta verrà inglobato dalla stella morente; altri invece ipotizzano
	che il pianeta si salverà. Il nostro pianeta sarà però inabitabile: gli oceani saranno evaporati a causa del forte calore e gran parte
	dell'atmosfera verrà dispersa nello spazio dall'intensa energia termica. Tutto ciò avverrà entro i prossimi 3,5 miliardi di anni. Entro 7,8
	miliardi di anni, esaurito ogni processo termonucleare, il Sole rilascerà i suoi strati più esterni, che verranno spazzati via sotto forma di
	"supervento" creando una nebulosa planetaria; le parti più interne collasseranno e daranno origine ad una nana bianca (con dimensioni circa
	uguali a quelle della Terra), che lentamente si raffredderà sino a diventare, nel corso di centinaia di miliardi di anni, una nana nera.
</p>

<div id="titoloContent">

	<h2><strong> Caratteristiche e rotazione </strong></h2>

</div> <!-- fine secondo titolo content> -->

<p>
	Il Sole, come ogni altro corpo celeste nell'Universo, è costituito da elementi chimici, in gran parte da idrogeno ed elio. Gli elementi
	pesanti, detti convenzionalmente metalli, che si trovano soprattutto negli strati più superficiali, sono: litio, berillio, boro e neon. Il
	Sole è una sfera di plasma quasi perfetta, le cui dimensioni sono un po' più grandi di quelle di una stella di media grandezza, ma
	decisamente più piccole di quelle di una ben più imponente gigante blu o gigante rossa. Il suo diametro polare differisce da quello
	equatoriale di appena 10km. Poiché si trova allo stato di plasma e non possiede una superficie solida, la stella è soggetta ad una rotazione
	differenziale, ovvero ruota in maniera diversa a seconda della latitudine: infatti la stella ruota più velocemente all'equatore che non ai
	poli ed il periodo di rotazione varia tra i 25 giorni dell'equatore e i 35 dei poli.
</p>


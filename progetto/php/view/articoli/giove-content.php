	<h2><strong> Anelli e satelliti </strong></h2>

</div> <!-- fine secondo titolo content> -->

<div align="center"><img class="principale" alt="I satelliti di Giove" src="images/giove-lune.jpg" width="550" height="187"></div>

<p>
	Oltre a una corte di numerosi satelliti (attualmente se ne conoscono 63), Giove ha un anello di materiali simile a quello di Saturno.
	Invisibile al telescopio, è stato rivelato per la prima volta dalla sonda <em>Voyager</em>, è spesso solo 4km e orbita attorno al pianeta
	a una distanza di circa 60.000km dalla sommità delle nubi.</br>
	Fra i satelliti di Giove si ricordano i piu grandi: <strong>Io</strong>, <strong>Europa</strong>, <strong>Ganimede</strong>,
	<strong>Callisto</strong>.
	</br>
	Essi furono scoperti da Galileo nel 1610 e denominati <em>"satelliti medicei"</em> in omaggio alla casata fiorentina. Facilmente osservabili
	con un buon binocolo, appaiono come piccole stelline attorno al pianeta ma, poichè il piano delle loro orbite è poco inclinato rispetto a
	quello di Giove, le eclissi sono molto frequenti. Fu studiando i loro moti che Ole Romer, nel 1675, riuscì a misurare per la prima volta la
	velocità della luce. Grazie alle esplorazioni delle sonde statunitensi <em>Pioneer 10 e 11</em>, <em>Voyager 1 e 2</em> e <em>Galileo</em>,
	abbiamo molte informazioni sui satelliti di Giove. I più piccoli sono simili alle lune degli altri pianeti, e a volte è evidente che
	hanno origine da asteroidi, mentre i satelliti medicei sono sferici, e molto diversi l'uno dall'altro.</br>
	<strong>IO</strong>: relativamente calda (2000K nei punti eruttivi, ma mediamente intorno ai 130K), ricca di vulcani attivi non ha crateri
	meteoritici. La sua superficie dunque, è molto recente: Io è l'unico corpo del sistema solare su cui si sia fotografata un'eruzione. Il
	materiale eruttato è probabilmente zolfo liquido, o silicati fusi ricchi di sodio. Alcuni dati recenti inviati dalla sonda Galileo
	suggeriscono che Io abbia un nucleo di ferro di almeno 900km di diametro e un proprio campo magnetico. L'origine dell'energia di Io sarebbe
	nelle interazioni di marea che la legano a Europa, Ganimede e a Giove: mentre Io volge la stessa faccia a Giove ( come accade alla Luna con la
	Terra) la gravità di Europa e di Ganimede ne deformano la crosta con dislivelli anche di 100m d'altezza. Queste deformazioni continue si
	traducono in energia termica, proprio come accade a un fil di ferro piegato ripetutamente.</br>
	<strong>EUROPA</strong>: è di poco più piccolo della Luna ed è completamente avvolto dal ghiaccio, al di sotto del quale, la sonda Galileo, ha
	scoperto un vasto mare salato, ancora liquido per il calore sviluppato dalle interazioni di marea. Europa è il corpo più liscio del sistema
	solare: le formazioni superficiali più elevate, infatti, non superano qualche centinaio di metri. Insieme a Io, Ganimede, Titano e Tritone,
	Europa è tra i pochi satelliti dotati di atmosfera e possiede anche un debole campo magnetico che, per la sua rotazione intorno a Giove, varia
	continuamente sotto l'influenza del forte campo magnetico del pianeta.</br>
	<strong>GANIMEDE</strong>: è il più grande satellite del sistema solare e supera anche Mercurio. Sotto una crosta di ghiaccio spessa da 100 a
	200 km nasconde uno strato d'acqua liquida, mentre all'esterno ha un aspetto lunare, con distese pianeggianti, crepacci e crateri
	pianeggianti di ogni dimensione. Ha un leggero campo magnetico.</br>
	<strong>CALLISTO</strong>: crivellato di crateri per le meteoriti catturate dal campo gravitazionale di Giove, mostra le tracce di un
	gigantesco impatto avvenuto in passato.
</p>

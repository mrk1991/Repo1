	<h1><strong> GIOVE </strong></h1>
	<h2><em>
		E' il più grande del sistema solare e il secondo dei pianeti esterni.La sua spessa atmosfera impedisce di vedere il pianeta vero e
		proprio, ma gli dona una luminosità che, a volte, uguaglia quella di Venere. Per molti aspetti è simile al Sole: ha una rotazione
		differenziale evidente, ha fenomeni superficiali di grandi dimensioni e breve durata ma,soprattutto, emette energia. Più di quanta
		non ne riceva dal Sole.
	</em></h2>
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Giove" src="images/giove.jpg" width="675" height="379"></div>

<p>
	Non è solo il pianeta più grande: dopo Venere, quando è in opposizione, è anche il pianeta più brillante. Giove si presta molto bene
	all'osservazione, già con un binocolo è possibile vederlo attorniato dai suoi 4 satelliti principali, mentre attraverso un telescopio o un
	buon binocolo si può notare che ha l'aspetto di un dischetto schiacciato ai poli attraversato da strisce chiare e scure parallele 
	all'equatore. Per distinguere ulteriori dettagli della superficie è necessario usare un buon telescopio: potremo allora osservare un rapido
	avvicendarsi di formazioni mutevoli che, nel giro di qualche giorno o addirittura qualche ora, insorgono e scompaiono, ma meglio ancora
	potremo vedere anche la famosa "macchia rossa", un uragano di immense proporzioni che agita l'atmosfera di Giove da almeno 3 secoli.
	Le massime misure di questo fenomeno superficiale sono state 39.000km per 14.000km: esso fu avvistato per la prima volta più di 300 anni fa,
	e i planetologi sono propensi a ritenere che, pur essendo un fenomeno instabile, abbia lunga durata. Anche la colorazione a fasce orizzontali
	è prodotta da forti venti e da movimenti convettivi che agitano i gas dell'atmosfera: nelle zone chiare i gas più caldi salgono rapidamente
	ad alta quota dove si cristallizzano, tornando a scendere nelle zone più scure. I colori dominanti – giallo, arancione, ocra – sono prodotti
	dall'ammoniaca e dall'idrosolfuro di ammonio che si condensano in spesse nubi, mentre i colori più intensi sono dovuti ai composti del fluoro.
</p>


<p>
	Dopo il Sole, Giove è il corpo più grande del sistema solare: rispetto al Sole ha un diametro 10 volte più piccolo, una massa 1000 volte
	inferiore e una densità quasi uguale. Il suo volume è ben 1300 volte quello della Terra, e la sua massa è tale da produrre sensibili
	perturbazioni nelle orbite degli asteroidi e delle comete. Il moto di rotazione intorno all'asse è molto veloce: in appena 9h 50min compie
	un giro completo. Ne consegue che all'equatore, la velocità lineare arriva a 12,6km/s; ciò provoca il notevole schiacciamento ai poli e
	l'evidente rotazione differenziale simile a quella che si verifica sul Sole: Giove non ruota come un corpo rigido, ma più velocemente
	all'equatore che ai poli. Con il Sole ha in comune anche i principali costituenti: gas come l'idrogeno (90%), e l'elio (9%), e in misura
	nettamente inferiore metano e ammoniaca. Inoltre, i dati raccolti dalle sonde <em>Pioneer</em>, <em>Galileo</em> e <em>Voyager</em> indicano
	come il pianeta si stia contraendo: ogni anno il suo diametro si riduce di circa 1mm. Questa considerazione e numerose altre confermano
	l'ipotesi che Giove sia un corpo prevalentemente gassoso, e che l'energia che esso emette in misura maggiore rispetto a quanta ne riceva dal
	Sole sia prodotta proprio dalla <em>“concentrazione gravitazionale”</em> evidenziata dalle sonde. Se Giove fosse stato un pò più grande
	questi processi avrebbero potuto sfociare in reazioni nucleari di fusione, proprio come è avvenuto sul Sole, e il nostro sistema sarebbe
	stato un sistema doppio. La massa di Giove invece non è sufficiente e per questo se ne parla come di una <em>“stella mancata”</em> e la sua
	temperatura resta considerevolmente bassa. Il veloce movimento di rotazione provoca anche altre conseguenze: innanzitutto ha determinato un
	equilibrio geostrofico tra la pressione atmosferica e la forza di <strong>Coriolis</strong>, ostacolando l'instaurarsi di correnti
	atmosferiche in direzione meridiana, inoltre, l'alta velocità di rotazione sarebbe la causa dell'intenso campo magnetico che ha origine dal
	pianeta. La magnetosfera di	Giove si estende nello spazio per milioni di kilometri, provocando bellissime aurore polari simili a quelle che
	si osservano anche sulla Terra. Sulla base dei dati raccolti dalle sonde, inoltre, si pensa che Giove abbia al suo interno un piccolo nucleo
	roccioso di silicati di ferro immerso in un oceano di idrogeno metallico liquido. Questa struttura contribuirebbe a spiegare il campo
	magnetico, ma è solo un'ipotesi di lavoro. Al di sopra, per quasi 1.000km, si estende l'atmosfera.
</p>


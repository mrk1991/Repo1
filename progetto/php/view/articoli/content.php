<p>
	L'astronomia, cioè lo studio dei corpi celesti, dei loro movimenti, dei fenomeni ad esso legati, è la scienza più antica: si
	può dire che è nata con l'uomo, intimamente legata alla sua natura di essere pensante, alla sua necessità di misurare il
	tempo, di “mettere ordine” nelle cose che conosce, al bisogno di orientarsi nei suoi viaggi o, più in generale, di
	padroneggiare la natura. Non è certo un caso che anche per i più antichi reperti archeologici si trovino sorprendenti
	corrispondenze astronomiche: Stonehenge, il monumento paleolitico più noto, è stato costruito sulle basi di una precisa
	conoscenza astronomica, ma non solo, lo stesso vale per i cromlech e i monoliti bretoni, per i triliti inglesi, per le
	pietre e i tumuli irlandesi, per la Casa Rinconada degli indiani Anasazi, per i siti maya di Uaxactun e Machu Pichu e per
	tanti altri. <br /> Più gli studi archeoastronomici procedono, più numerose diventano le prove sulle conoscenze astronomiche
	dei popoli del passato e più indietro nel tempo si sposta la data alla quale, si può far risalire la partecipazione certa
	dell'uomo ai principali eventi celesti. E' indubbio che la contemplazione del cielo notturno deve aver suscitato meraviglia,
	timore, interrogativi fin dai primordi: qual'è la natura dei corpi celesti? Perché si muovono,e come? Hanno effetti gli uni
	sugli altri? E soprattutto: influenzano la Terra e i destini dei suoi abitanti? Possiamo prevedere questi effetti, e leggere
	il futuro nel moto dei pianeti? A domande simili, ogni cultura, in ogni epoca, ha dato le proprie risposte.
</p>

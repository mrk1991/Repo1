<div id="titoloContent">

	<h2><strong> Il paesaggio marziano </strong></h2>

</div>

<p>
	Su Marte il paesaggio è più vario di quanto mostrino le immagini raccolte dalle sonde: nell'emisfero Nord, infatti, predominano regioni
	pianeggianti e desertiche, coperte di roccia rossastra; nell'emisfero Sud, invece, il territorio è profondamente accidentato, interrotto
	continuamente da numerosi crateri di origine meteoritica. <strong>Hellas</strong>, uno dei più grandi, ha una profondità di 3km e un diametro
	di 1.800km.</br>
	La regione <strong>Tharsis</strong>, poi, è attraversata da un'enorme frattura che si estende per circa 5.000km, pari a quasi un sesto della
	circonferenza dell'intero pianeta. E' la <strong>Valle Marineris</strong>, che a tratti si allarga oltre i 100km. I poli, con le loro calotte
	risplendenti di ghiaccio e di anidride carbonica solida, sono la formazione geologica più mutevole della superficie marziana. Esse, infatti,
	variano notevolmente di estensione al variare della stagione.
</p>


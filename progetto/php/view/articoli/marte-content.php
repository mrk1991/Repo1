	<h2><strong> I satelliti Phobos e Deimos </strong></h2>

</div>
	
<div align="center"><img class="principale" alt="I satelliti Phobos e Deimos" src="images/marte-phobos_deimos.jpg" width="400" height="380"></div>

<p>
	Sono i 2 satelliti di Marte: <strong>Phobos</strong> (Paura) e <strong>Deimos</strong> (Terrore) ricordano rispettivamente il figlio e il
	compagno di Marte, dio greco della guerra. Non sono visibili con telescopi amatoriali: il loro diametro, rispettivamente di 25km e di 13km,
	è troppo piccolo. Per il loro aspetto e la bassa albedo (riflettività) si ritiene che siano meteoriti catturati dalla gravità marziana.
</p>

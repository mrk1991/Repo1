	<h1><strong> MARTE </strong></h1>

	<h2><em>
		Il pianeta rosso, chiamato così per il colore del suolo, ben visibile anche a occhio nudo, è il primo pianeta “esterno” all'orbita
		terrestre. Piccolino, molto freddo, per molti tratti ricorda la Terra: le calotte polari cambiano aspetto al variare delle stagioni,
		il suolo mostra tracce di fiumi e di un antico vulcanismo. E fin dall'antichità si sospetta che ospiti forme di vita.
	</em></h2>

</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Marte" src="images/marte.jpg" width="600" height="450"></div>

<p>
	Visto con un telescopio o un buon binocolo, appare come un piccolo disco rossastro: è per questa sua colorazione che viene chiamato fin
	dall'antichità <em>“pianeta rosso”</em>. Marte è molto più piccolo della Terra, ha un diametro che è circa la metà di quello del nostro
	pianeta e forse anche per questo non è molto adatto all'osservazione: infatti, nonostante sia un pianeta relativamente vicino (al perigeo
	dista poco più di 56*10⁶km) anche nelle migliori condizioni di visibilità non si possono distinguere i particolari della superficie. Si può
	vedere, in realtà, la sottile rete di strutture scure e regolari che Schiapparelli descrisse nel 1877: non si tratta di canali costruiti da
	esseri viventi, come egli la interpretò, ma di illusioni ottiche prodotte dalle turbolenze atmosferiche. Su Marte, infatti, non si è trovata
	alcuna evidenza di vita.
	</br>
	Marte è sempre stato considerato molto simile alla Terra: la durata del giorno è quasi uguale a quella terrestre poiché il periodo di
	rotazione marziano è solo di poco più lungo di quello del nostro pianeta (circa 24h e mezzo). Inoltre, poiché il piano equatoriale del
	pianeta è inclinato rispetto a quello della sua orbita di quasi 25°, come sulla Terra anche su Marte le differenze stagionali durante
	l'anno sono notevoli.</br>
	Le somiglianze però finiscono qui: come è accaduto nel caso di Venere, tutti i dati raccolti dalle sonde sottolineano solo l'enorme
	differenza tra Marte e il nostro pianeta. La sua massa, ad esempio, è solamente un decimo di quella della Terra, e quindi anche la gravità è
	molto minore; la temperatura oscilla fra le molte decine di gradi sottozero e i pochi gradi sopra lo zero, con grandi escursioni termiche
	giornaliere (anche 50° di differenza tra notte e giorno). A causare queste forti differenze termiche contribuisce anche l'atmosfera
	estremamente rarefatta: circa 100 volte meno densa di quella terrestre, essa non è in grado di sviluppare un effetto serra sensibile, pur
	essendo costituita prevalentemente di anidride carbonica.</br>
	Per le basse temperature raggiunte, specialmente ai poli, questo gas solidifica durante l'inverno e fonde durante l'estate. Non è certo
	un'estate torrida: in media la temperatura oscilla sui -68°C, ma è già sufficiente a ridurre notevolmente le calotte polari.</br>
	Le drammatiche escursioni termiche generano vere e proprie <em>“maree atmosferiche”</em>, con correnti molto più ampie di quelle che si
	verificano sulla Terra. Pur essendo molto rarefatta, l'atmosfera marziana in movimento provoca immense tempeste di sabbia che,
	periodicamente, avvolgono l'intero pianeta. La superficie di Marte è quindi desertica, ricoperta di una polvere di ossido di ferro, dal
	colore rosso ruggine. Sollevata dai venti, questa polvere dà una colorazione rosata anche all'atmosfera marziana: lo si vede molto bene anche
	nelle riprese inviate a terra dalle sonde statunitensi che sono riuscite ad atterrare sul pianeta.
</p>

<div id="titoloContent">

	<h2><strong> Geologia </strong></h2>

</div>

<p>
	E' molto probabile che, un tempo, Marte abbia avuto un aspetto ben diverso.</br>
	Larghi canyon e pianure di tipo alluvionale dimostrano che il suolo è stato modellato da un fluido in scorrimento. L'ipotesi più probabile è
	che si sia trattato di acqua: alcuni miliardi di anni fa, Marte aveva fiumi, presumibilmente a carattere torrentizio, che hanno lasciato il
	disegno del loro percorso sulla superficie rocciosa del pianeta. Di quest'acqua, però, non vi è più alcuna traccia: non se ne trova allo
	stato liquido, né allo stato gassoso, ma potrebbe essere presente nel sottosuolo in forma di ghiaccio.</br>
	Le evidenze di un'intensa attività vulcanica sono numerose e indubbie: nella fascia equatoriale, le due vaste regioni di <strong>Tharsis
	</strong> e <strong>Elysium</strong> presentano un'alta concentrazione di vulcani spenti di notevole dimensioni: il Monte Olimpo, il più
	grande, supera i 26km di altezza, più alto di tre Everest messi uno sull'altro! Le immense colate laviche che li circondano (la base del
	Monte Olimpo da sola coprirebbe tutto lo stato dell'Arizona) dimostrano che, in passato, l'attività endogena del pianeta è stata
	considerevole. Forse, come è accaduto sulla Luna, la minor gravità ha favorito l'allargarsi delle colate laviche; tuttavia è indubbio che la
	mole di materiali eruttati è stata spesso così rilevante da cancellare, in molte zone, ogni traccia dei crateri meteoritici.
</p>


<p>
	Al binocolo questo pianeta appare come un punto luminoso, mentre al telescopio assume una colorazione verde-blu, pur rimanendo un disco
	troppo piccolo perchè si possano distinguere dettagli significativi. E' un pianeta scoperto per caso da <em>William Herschel</em> che, nel
	1781, stava osservando in modo sistematico il cielo con il suo nuovo telescopio. Per la sua piccola magnitudine, anche se era stato visto
	dagli antichi astronomi era stato considerato una stella fissa. Urano è un pianeta unico nel sistema solare: oltre ad avere un moto rotatorio
	retrogrado, il suo asse di rotazione è inclinato di 82° rispetto al piano dell'orbita: in altre parole, l'equatore è quasi perpendicolare
	alla direzione del moto di rivoluzione. Si suppone che un violento impatto con un corpo delle dimensioni della Terra lo abbia “ribaltato”
	così a partire da una posizione simile a quella degli altri pianeti. Un urto di questa energia spiegherebbe anche il moto retrogrado: se il
	pianeta si fosse ribaltato di 98°, il Polo Nord sarebbe quello sotto il piano dell'orbita, e il pianeta starebbe ancora girando “nel verso
	giusto”. Le conseguenze di questa caratteristica sono numerose: il polo esposto al Sole ha una temperatura media di -208°C (62,4K) mentre il
	polo nascosto al Sole ha una temperatura media di -215°C (64,5K); diversamente da ciò che accade agli altri pianeti, i poli sono osservabili,
	così come il sistema di 11 anelli concentrici, formati di polveri e minuscoli frammenti solidi che resta quasi sempre completamente visibile.
	Inoltre, il campo magnetico, che ha un'intensità di circa 1/3 di quello terrestre (0,25G) e ha un asse inclinato di 35° rispetto a quello di
	rotazione.</br>
	Come i grandi pianeti esterni, Urano ha un'atmosfera molto sviluppata, composta da idrogeno ed elio ma anche da metano, che gli dà un colore
	aranciato-azzurro. La sonda <em>Voyager 2</em> ha mostrato le nubi dell'alta atmosfera, dove la temperatura, vicina allo zero assoluto,
	oscilla sui -200°C.</br>
	Come Saturno, oltre agli anelli, Urano ha un corredo di numerosi satelliti: da terra se ne vedono 5, chiamati <strong>Miranda, Ariel, Umbriel,
	Titania</strong> e <strong>Oberon</strong>, ma la sonda <em>Voyager 2</em> ne ha scoperto altri 10 ed è probabile che ce ne siano ancora,
	anche se 9 dei nuovi satelliti sono semplici rocce cosmiche catturate dal pianeta in tempi remoti. Chiamati come i personaggi di
	<em>Shakespeare</em> e di <em>Pope</em>, i satelliti di Urano orbitano con moto retrogrado lungo il piano equatoriale. Si ritiene che, come
	gli altri sistemi di satelliti dei grandi pianeti, anche la loro evoluzione sia rimasta bloccata ad alcuni miliardi di anni or sono.
</p>

	<h1><strong> URANO </strong></h1>

	<h2><em>
		Nelle notti senza Luna, Urano è quasi invisibile a chi guarda il cielo senza l'aiuto di uno strumento. É un pianeta “moderno” che ha
		assunto il suo nome definitivo solo nel 1850. Ma è alla sonda Voyager 2, che lo ha avvicinato e fotografato, che Urano ha mostrato il
		suo bellissimo aspetto e alcune delle sue più importanti caratteristiche.
	</em></h2>


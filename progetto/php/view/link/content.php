<div id="titoloContent">
    <h2><strong> Astronomia su Internet </strong></h2>
</div> <!-- fine titoloContent -->

<p>
	In rete, l'astronomia è uno degli argomenti più diffusi. I siti dedicati a questa materia sono innumerevoli. Di seguito è riportato un
	piccolo elenco di indirizzi particolarmente interessanti dove trovare informazioni scientifiche, news e immagini dello spazio.
	<ul>
		<li><a href="http://www.esa.int/esaCP/">www.esa.int/esaCP/</a> - ESA (Agenzia Spaziale Europea). Immagini di satelliti, news sui programmi
			spaziali, links ecc.</li></br>
		<li><a href="http://www.nasa.gov/">www.nasa.gov/</a> - NASA (Agenzia Spaziale Americana). Esplorazione dello spazio, missioni e
			immagini.</li></br>
		<li><a href="http://www.eso.org/public/">www.eso.org/public/</a> - ESO (European Southern
			Observatory). L'Osservatorio Astronomico Europeo Meridionale (situato in Cile), uno dei più importanti del mondo.</li></br>
		<li><a href="http://www.skyandtelescope.com/">www.skyandtelescope.com/</a> - Sky & Telescope è la più prestigiosa rivista mondiale di
			divulgazione astronomica. Nel suo sito sono presenti numerose e aggiornate rubriche on-line.</li></br>
		<li><a href="http://www.seti.org/">www.seti.org/</a> - Il sito ufficiale del SETI (Search for Extra Terrestrial Intelligence). Per chi
			vorrebbe contattare gli extraterrestri.</li></br>
		<li><a href="http://nineplanets.org/">http://nineplanets.org/</a> - Panoramica della storia e delle attuali conoscenze relative ai
			pianeti e alle lune del Sistema Solare.</li></br>
		<li><a href="http://astrolink.mclink.it/sist_sol.html">http://astrolink.mclink.it/sist_sol.html</a> - La pagina di Wikipedia sul Sistema
			Solare, punto di partenza per navigazioni senza fine.</li></br>
		<li><a href="http://www.stsci.edu/portal/ ">www.stsci.edu/portal/ </a>- Il sito ufficiale del Telescopio Spaziale Hubble, con le
			straordinarie immagini di questo ineguagliabile strumento.</li></br>
		<li><a href="http://www.iau.org/">www.iau.org/</a> - Se scoprite una cometa, un asteroide o un nuovo pianeta, dovete rivolgervi alla IAU
			(International Astronomical Union).</li></br>
		<li><a href="http://skyview.gsfc.nasa.gov/cgi-bin/titlepage.pl">http://skyview.gsfc.nasa.gov/cgi-bin/titlepage.pl</a> - Un osservatorio
			astronomico virtuale per tutte le zone del cielo a ogni lunghezza d'onda.</li>
	</ul>
</p>

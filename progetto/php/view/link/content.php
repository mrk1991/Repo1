    <h1><strong> Link utili </strong></h1>
</div> <!-- fine titoloContent -->

<p>
	Per entrare nel mondo dell'astronomia, far da soli non è indicato: l'esperienza, lo scambio di idee e di opinioni, i suggerimenti di chi è già
    avanti in questo campo per lavoro o per passione sono essenziali. Così come è essenziale avere voglia di leggere, di documentarsi, di cercare
    nuove informazioni, magari anche su ciò che si pensava di conoscere bene.  Lo studio del cielo, dei suoi moti, dei corpi che lo popolano, lo
    studio della stessa storia che ha portato alle attuali conoscenze sono solo un percorso infinito, un punto di partenza per nuove scoperte. Qui
    abbiamo voluto dare qualche "dritta" a chi volesse ampliare i propri orizzonti, un aiuto minimo per proseguire nel cammino iniziato. 
</p>


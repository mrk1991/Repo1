<p>
    Ormai non guardiamo più il cielo. Affogati nelle luci delle nostre città, in corsa frenetica fra mille cose da fare, ipnotizzati dalla
    televisione, non abbiamo più tempo di guardare nella notte. Abbiamo dimenticato la bellezza delle stelle, il lento scorrere dei pianeti, i
    ritmi lunari e l'alternarsi delle costellazioni, stagione dopo stagione. E' il senso di questo sito: catturare con le immagini la fantasia per
    stimolare chi legge a riappropriarsi di quelle conoscenze che, solo un secolo fa, erano patrimonio di tutti. Dirgli come usare un binocolo per
    riscoprire la Luna e le fasi di Venere, o semplicemente permettergli di trovare la propria costellazione zodiacale. E in questo modo portarlo a
    rimanere affascinato, come è capitato a migliaia di studiosi e appassionati prima di lui, da una scienza che è stata la prima, vera scienza
    dell'uomo. Portarlo a guardare oltre la Terra, con gli occhi dei potenti strumenti moderni, e a vedere il Sole, i pianeti, le stelle sotto
    prospettive inattese; a scoprire l'esistenza di nebulose evanescenti e galassie che generano stelle, e si scontrano, e viaggiano a velocità
    inimmaginabili in un universo di cui si può solo teorizzare un inizio o una fine. E' un sito per chi vuole passare qualche serate in compagnia
    dell'infinito, o aprofittare di una vacanza per riscoprire, nei cieli puliti, neri e profondi delle località isolate, un mondo che ci pare
    estraneo, lontano e silenzioso, e che pure ci comprende e ci dà, ogni giorno, la vita.
</p>

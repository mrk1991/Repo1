    <h1><strong> Gli strumenti per l'astronomia </strong></h1>
</div> <!-- fine titoloContent -->

<div align="center"><img class="principale" alt="Telescopio" src="images/telescopio.jpg"></div>

<p>
	Chi si avvicina all'astronomia, prima o poi inizia a valutare la possibilità di acquistare uno strumento. In effetti, a occhio nudo, si
	possono già fare delle osservazioni notevoli, tuttavia, quando si vuole scendere nel dettaglio, è necessario aiutarsi con uno strumento
	ottico. Il consiglio che si può dare prima di acquistarne uno è di procedere per gradi. 
</p>

<div id="titoloContent">
    <h2><strong> Binocoli e telescopi </strong></h2>
</div> <!-- fine titoloContent -->

<img class="sinistra" alt="Telescopi che guardano il cielo" src="images/telescopio2.jpg" width="300" height="233">

<p>
	Il <strong>binocolo</strong> da usare per osservare i corpi celesti deve possedere alcune caratteristiche fondamentali. Prima di tutto
	bisogna saper leggere le indicazioni che riassumono le caratteristiche ottiche dello strumento: sono i numeri incisi (ad esempio 8x30) che
	indicano rispettivamente gli ingrandimenti e il diametro della lente anteriore in millimetri. Poichè a maggiori ingrandimenti corrisponde una
	diminuzione del campo visivo e della luminosità, è inutile usare un binocolo con molti ingrandimenti per osservare una stellina fioca: non la
	vedremmo certo meglio che a occhio nudo. Inoltre, per un valore d'ingrandimento superiore a 10 è necessario usare un treppiede. Ingrandire,
	poi, serve solo se si guardano sorgenti non puntiformi: la Luna, i pianeti, le comete, le nebulose. Osservare le stelle serve a poco: data la
	distanza, qualunque sia l'ingrandimento, una stella appare sempre come un puntino luminoso sfocato. In questi casi conta di più la
	luminosità: se non raccoglie abbastanza luce, il binocolo non può servire per l'osservazione notturna. E' l'apertura, cioè il diametro della
	lente, il fattore principale. Tenuto conto di tutte le variabili, il binocolo più consigliabile è 7x50. Va considerato anche che quando un
	binocolo pesa più di 1kg diventa difficile tenerlo fermo: diventa indispensabile montarlo su un cavalletto, che oltre a costare impedisce i
	movimenti. Per di più, anche se	esistono binocoli con 4, 7, 10, 20, 30 ingrandimenti, non c'è un grande vantaggio a usare quelli più potenti,
	visto che un piccolo telescopio, a parità di prezzo può dare 100 o 200 ingrandimenti. Va valutato bene perciò, se è o no il caso di
	acquistare un <strong>telescopio</strong>. Anche questo strumento, però, ha i suoi pro e i suoi contro. Pur se piccolo, è sempre ingombrante
	e pesante. Tuttavia in commercio si trovano buoni telescopi, con dimensioni contenute e a prezzi ragionevoli. La scelta migliore potrebbe
	essere uno <em>Schmidt-Cassegrain</em> con specchio di 20cm di diametro: per un peso di circa 10kg, con un opportuno ingrandimento ci
	consente di osservare dettagli intorno ai 2 d'arco e, in condizioni particolarmente buone, oggetti 600 volte più deboli di quelli al limite
	di visibilità. Le caratteristiche di un telescopio, sia esso riflettore o rifrattore, sono la luminosità, l'ingrandimento e il potere
	risolutivo.
</p>
<p>
	La <strong>luminosità</strong> è un numero proporzionale alla radice quadrata degli erg per secondo e per unità di superficie che lo
	strumento è in grado di concentrare sul suo piano focale. Fissata la lunghezza d'onda, nel caso di stelle è pari al rapporto fra il quadrato
	del diametro dell'obiettivo (o dello specchio) e la focale, mentre per oggetti estesi è data dal rapporto fra il diametro dell'obiettivo e la
	focale. La possibilità di vedere stelle "fioche" dipende dalla luminosità, non dall'ingrandimento.</br></br>
	L'<strong>ingrandimento</strong>, che si intende sempre angolare, è il rapporto tra la focale dell'obiettivo e la focale dell'oculare: quanto
	questa è più corta, tanto maggiore è l'ingrandimento. Questa grandezza è rilevante solo quando si osservano oggetti non puntiformi.</br></br>
	Il <strong>potere di risoluzione</strong> è l'angolo minimo sotto cui le immagini di due punti si vedono ancora separate e distinte: un alto
	potere risolutivo permette di vedere immagini nitide e dettagli minuti. Esso è direttamente proporzionale alla lunghezza d'onda della
	radiazione esaminata e inversamente proporzionale all'apertura assoluta. Anche questa grandezza è rilevante solo nel caso di oggetti non
	puntiformi. A limitare la possibilità di osservazione, però, è il <em>seeing</em>: tutto l'insieme di condizioni di visibilità atmosferica,
	strumentale ecc. che varia da luogo a luogo e da istante a istante.
</p>
<p>
	<img class="sinistra" alt="I diversi tipi di telescopio" src="images/tipi_telescopio.jpg">
	Un telescopio ottico è costituito da un tubo che a un'estremità ha un <em>obiettivo</em> (cioè un insieme di lenti) o uno <em>specchio</em>.
	L'obiettivo fornisce un'immagine dell'oggetto osservato che può essere vista, fotografata, o analizzata da strumenti adatti attraverso un
	<em>oculare</em>. Se la radiazione viene raccolta da una lente (o da un insieme di lenti), il telescopio è detto <strong>rifrattore</strong>,
	se viene raccolta da uno specchio, è detto <strong>riflettore</strong>. I telescopi che combinano lenti e specchi sono detti
	<strong>catadiottrici</strong>.</br>
	Importante è anche la <strong>montatura</strong> di un telescopio, cioè l'articolazione con cui il tubo del telescopio può essere orientato e
	mosso. Quella usata più comunemente è la montatura <em>equatoriale</em>, che permette di usare le coordinate del sistema equatoriale fisso.
	Il telescopio infatti può ruotare sia intorno a un asse parallelo all'asse terrestre, sia intorno a un asse ad esso perpendicolare (parallelo
	al piano dell'equatore celeste). Su un cerchio concorde con il primo asse, diviso in 360° e frazioni di grado, si leggono le
	<em>declinazioni</em>; su un cerchio concorde con il secondo asse e diviso in ore e frazioni d'ora si leggono gli <em>angoli orari</em>. Una
	volta fatto il puntamento, un congegno a orologeria fa ruotare il telescopio in modo concorde con l'oggetto osservato.
</p>
